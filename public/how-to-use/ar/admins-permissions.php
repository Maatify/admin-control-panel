<ul>
<li><strong>التغييرات فورية:</strong> كن متأكدًا تمامًا قبل النقر فوق تعيين أو إبطال.</li>
<li><strong>تؤثر الأذونات على الرؤية:</strong> سيؤدي منح الأذونات أو إلغاؤها إلى إضافة أو إزالة روابط الشريط الجانبي وأزرار الإجراءات على الفور من واجهة المسؤول.</li>
<li><strong>يجب استخدام الأذونات المباشرة باعتدال:</strong> اعتمد على الأدوار في 99% من إدارة الوصول الخاصة بك. يجب حجز الأذونات المباشرة بشكل صارم للاستثناءات المؤقتة أو المتطلبات الإدارية الفريدة للغاية.</li>
</ul>
