<h1>إدارة الأذونات</h1>
<h2>1. نظرة عامة</h2>
<p>تعرض هذه الصفحة جميع الأذونات التي تؤثر حاليًا على مسؤول معين. إنه بمثابة المصدر النهائي للحقيقة للتحكم في الوصول، حيث يجمع الأذونات الممنوحة بشكل غير مباشر من خلال الأدوار مع الأذونات الممنوحة عبر الأذونات المباشرة. وهو يعكس سلوك الوصول الحقيقي وحدود الأمان الدقيقة المطبقة حاليًا على المسؤول في النظام.</p>
<h2>2. كيفية الوصول</h2>
<p>لإدارة أذونات المسؤول أو عرضها:
1. انتقل إلى <strong>المشرفين</strong> في الشريط الجانبي الأيسر.
2. انقر <strong>منظر</strong> بجانب المسؤول المحدد.
3. انقر فوق <strong>الأذونات</strong> الرابط/القسم.</p>
<ul>
<li>يمكنك استخدام شريط التنقل الموجود أعلى الصفحة للعودة بسهولة إلى قائمة المسؤولين الرئيسية.</li>
<li>يمكنك النقر فوق <strong>العودة إلى الملف الشخصي</strong> زر للعودة إلى عرض الملف الشخصي الرئيسي للمسؤول.</li>
</ul>
<h2>3. شرح علامات التبويب</h2>
<p>تنقسم صفحة الأذونات إلى ثلاث علامات تبويب مهمة لمساعدتك على فهم كيفية منح الوصول وإدارتها بالضبط.</p>
<h3>علامة التبويب الفعالة</h3>
<ul>
<li><strong>ماذا يفعل:</strong> يعرض الأذونات النهائية المحسوبة المطبقة على المسؤول.</li>
<li><strong>ما يتضمنه:</strong> تجمع هذه القائمة بين الأذونات المستندة إلى الأدوار والأذونات المباشرة في عرض شامل واحد.</li>
<li><strong>عمود المصدر:</strong> يتضمن عمود "المصدر" محددًا يخبرك بالضبط بالمصدر الذي يأتي منه الإذن (على سبيل المثال، ما إذا كان قد تم منحه بواسطة دور محدد أو عبر تجاوز السماح المباشر).</li>
<li><strong>للقراءة فقط:</strong> علامة التبويب هذه مخصصة للتدقيق فقط. لا يوجد أي تعديل أو تعيين يتم من علامة التبويب هذه.</li>
</ul>
<h3>علامة التبويب المباشرة</h3>
<ul>
<li><strong>ماذا يفعل:</strong> يعرض فقط الأذونات التي تم تعيينها مباشرة لهذا المسؤول الفردي، متجاهلاً تمامًا أي أدوار يشغلها.</li>
<li><strong>ما يتضمنه:</strong> وهو يشرح بالتفصيل النوع (سواء كان الإذن المباشر عبارة عن تجاوز "مسموح به" أو تجاوز "مرفوض") والطابع الزمني "ممنوح عند".</li>
<li><strong>الإجراءات:</strong> من هنا يمكنك النقر على <strong>تعيين إذن</strong> لإضافة تجاوز مباشر جديد، أو استخدم الزر <strong>يحرر</strong> و <strong>إبطال</strong> أزرار الإجراءات الموجودة في الصفوف الموجودة. يؤدي الإلغاء ببساطة إلى إزالة التعيين المباشر؛ ولا يمنع الإذن إذا كان المستخدم لا يزال يحتفظ به عبر الدور.</li>
</ul>
<h3>علامة تبويب الأدوار</h3>
<ul>
<li><strong>ماذا يفعل:</strong> يعرض أدوار الأمان المحددة المعينة حاليًا للمسؤول.</li>
<li><strong>ما يتضمنه:</strong> يسرد اسم الدور وحالته الحالية (نشط أو غير نشط).</li>
<li><strong>إيضاح:</strong> الأدوار تمنح الأذونات بشكل غير مباشر. يؤدي تعيين دور إلى مسؤول إلى منحه جميع الأذونات المجمعة داخل هذا الدور، والتي سيتم ملؤها بعد ذلك في علامة التبويب الفعالة.</li>
</ul>
<h2>4. المرشحات والبحث</h2>
<p>لمساعدتك في العثور على حقوق وصول محددة، تتوفر الأدوات التالية عبر علامات التبويب:</p>
<ul>
<li><strong>مدخلات البحث:</strong> يمكنك تصفية الجداول عن طريق الكتابة في مربعات الإدخال المحددة المتوفرة <strong>معرف الإذن</strong>, <strong>اسم الإذن</strong>، و <strong>مجموعة</strong>.</li>
<li><strong>زر إعادة الضبط:</strong> يتم توفير زر لمسح جميع عوامل التصفية النشطة على الفور وإعادة الجدول إلى طريقة العرض الافتراضية الخاصة به.</li>
<li><strong>شريط بحث الجدول:</strong> يوجد شريط بحث عالمي للبحث بسرعة عبر الجدول الحالي بأكمله.</li>
<li><strong>أزرار التصدير:</strong> يمكنك تصدير العرض الحالي لجدول الأذونات باستخدام الملف <strong>CSV</strong>, <strong>اكسل</strong>، و <strong>قوات الدفاع الشعبي</strong> أزرار التصدير.</li>
</ul>
<h2>5. تعيين تدفق الإذن</h2>
<p>لمنح إذن محدد أو حظره مباشرة لهذا المسؤول، وتجاوز أدواره:</p>
<ol>
<li>انقر فوق <strong>تعيين إذن</strong> الزر الموجود في علامة التبويب "مباشر".</li>
<li>يتم فتح مشروط كبير بعنوان <strong>الأذونات القابلة للتعيين</strong>.</li>
</ol>
<p>داخل المودال:
* سيظهر لك جدول مرقّم وقابل للبحث لكل إذن متوفر في النظام.
* الأعمدة المعروضة في هذا الشكل هي: <strong>بطاقة تعريف</strong>, <strong>اسم</strong>, <strong>مجموعة</strong>, <strong>اسم العرض</strong>, <strong>مُكَلَّف</strong>, <strong>يكتب</strong>، و <strong>تنتهي في</strong>.
*   <strong>فعل:</strong> يحتوي كل صف على زر تبديل أو زر لتعيين الإذن.
*   <strong>إعدادات:</strong> عند التعيين، يمكنك ضبط النوع على <strong>مسموح</strong> (لمنح حق الوصول) أو <strong>رفض</strong> (لمنع الوصول بشكل صريح، حتى لو كان الدور يمنحه). يمكنك اختياريا تحديد تاريخ انتهاء الصلاحية.
*   <strong>نتيجة:</strong> بمجرد تعيينه، يظهر الإذن على الفور في علامة التبويب "مباشر" ويعيد حساب الوصول النهائي للمسؤول في علامة التبويب "الفعال".</p>
<h2>6. التحرير/الإلغاء</h2>
<p>من <strong>مباشر</strong> علامة التبويب، يمكنك تعديل المهام المباشرة الموجودة باستخدام الأزرار الموجودة في عمود الإجراءات:</p>
<ul>
<li><strong>زر التحرير:</strong> يسمح لك بتعديل المهمة الحالية. يمكنك تغيير نوعه (قلب "السماح" إلى "الرفض") وتحديث أو إزالة تاريخ انتهاء الصلاحية الخاص به.</li>
<li><strong>زر الإلغاء:</strong> يؤدي النقر فوق هذا إلى حذف تعيين الإذن المباشر بالكامل.</li>
</ul>
<p><em>تأثير:</em> التغييرات نافذة المفعول على الفور. إذا قمت بإلغاء تجاوز "الرفض"، وكان لدى المشرف دور يسمح بهذا الإذن، فسوف يستعيد إمكانية الوصول على الفور.</p>
