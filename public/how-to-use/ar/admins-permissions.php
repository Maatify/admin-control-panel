<h2>8. قواعد السلوك</h2>
<ul>
<li><strong>فعالة = النتيجة النهائية:</strong> علامة التبويب الفعالة هي الحقيقة المطلقة لما يمكن للمسؤول فعله وما لا يمكنه فعله الآن.</li>
<li><strong>سلوك دور التجاوزات المباشرة:</strong> إذا منح الدور إذنًا، ولكن الإذن المباشر يرفضه، فإن الإذن المباشر هو الذي يفوز.</li>
<li><strong>رفض التجاوزات يسمح بما يلي:</strong> سيؤدي "الرفض" الصريح دائمًا إلى تجاوز "السماح"، بغض النظر عن عدد الأدوار التي تمنح "السماح".</li>
<li><strong>تحديثات واجهة المستخدم على الفور:</strong> يؤدي تبديل الأذونات أو تعيينها أو إبطالها إلى تحديث الجداول وحقوق الوصول على الفور.</li>
<li><strong>تؤثر التغييرات على المشرف على الفور:</strong> إذا كان المسؤول قد قام بتسجيل الدخول حاليًا، فإن تعديل أذوناته سيؤدي على الفور إلى إظهار أو إخفاء الأزرار وروابط الشريط الجانبي على شاشته.</li>
</ul>
<h2>9. أمثلة الاستخدام الحقيقي</h2>
<ul>
<li><strong>إضافة إذن مباشر لتجاوز الدور:</strong> يتمتع المسؤول بدور "الدعم" الذي لا يسمح بحذف المستخدمين. أنت في حاجة إليها لحذف حساب بريد عشوائي محدد اليوم. انقر فوق "تعيين إذن"، والعثور على <code>users.delete</code>، وقم بتعيينه على أنه "مسموح به" مع تحديد تاريخ انتهاء الصلاحية للغد.</li>
<li><strong>إلغاء الإذن الممنوح عن طريق التعيين المباشر:</strong> ينهي المسؤول المهمة مبكرًا. تذهب إلى علامة التبويب المباشرة الخاصة بهم، وتبحث عن <code>users.delete</code> تجاوز، ثم انقر فوق "إبطال". يفقدون على الفور القدرة على حذف المستخدمين.</li>
<li><strong>رؤية مصدر الإذن في علامة التبويب الفعالة:</strong> تريد أن تعرف <em>لماذا</em> يمكن للمسؤول الوصول إلى صفحة الفواتير. يمكنك التحقق من علامة التبويب الفعالة، والبحث عن <code>billing.view</code>وانظر إلى عمود "المصدر" لمعرفة الدور المحدد الذي يمنحهم هذا الوصول.</li>
</ul>
