<h1>إدارة نطاقات الترجمة</h1>
<h2>1. ما هو النطاق</h2>
<p>النطاق هو مساحة الاسم والفئة ذات المستوى الأعلى في نظام الترجمة. إنه يمثل حدود التطبيق الرئيسية أو البيئة. توجد نطاقات لتنظيم النص بشكل منطقي بحيث لا تتداخل أو تتعارض الترجمات الخاصة بأجزاء مختلفة تمامًا من النظام.</p>
<ul>
<li><strong>أمثلة:</strong> تتضمن النطاقات الشائعة في النظام عادةً <code>admin</code> (النص مخصص للوحة التحكم الإدارية)، <code>client</code> (نص لتطبيق المستخدم النهائي)، أو <code>api</code> (تم إرجاع النص في استجابات واجهة برمجة التطبيقات).</li>
</ul>
<h2>2. دور النطاقات في العمارة</h2>
<ul>
<li><strong>تَسَلسُل:</strong> تقع النطاقات في أعلى التسلسل الهرمي للترجمة: <strong>النطاقات ← المجالات ← المفاتيح ← القيم</strong>.</li>
<li><strong>المتطلبات الإلزامية:</strong> النطاق إلزامي تمامًا لأي ترجمة. يجب أن ينتمي كل مفتاح ترجمة في النظام إلى مجموعة نطاق ومجال محددة.</li>
<li><strong>التفرد:</strong> يلعب النطاق دورًا حاسمًا في التفرد الرئيسي. مفتاح (مثل <code>login.title</code>) يمكن أن يتواجد عدة مرات في النظام طالما أنه ينتمي إلى نطاق أو مجال مختلف. يفرض النظام التفرد بشكل صارم على مجموعة النطاق والمجال والمفتاح.</li>
</ul>
<h2>3. نموذج بيانات النطاق</h2>
<p>تتكون النطاقات من الخصائص التالية:</p>
<ul>
<li><strong>بطاقة تعريف:</strong> معرف النظام الفريد للنطاق.</li>
<li><strong>شفرة:</strong> معرف السلسلة الفريد الآلي المستخدم في النظام (على سبيل المثال، "admin").</li>
<li><strong>اسم:</strong> الاسم الذي يمكن قراءته بواسطة الإنسان والمعروض في واجهة المستخدم.</li>
<li><strong>وصف:</strong> البيانات الوصفية الاختيارية التي تشرح الغرض من النطاق.</li>
<li><strong>الحالة النشطة:</strong> علامة تحدد ما إذا كان النطاق ممكّنًا ومتاحًا حاليًا لدقة الترجمة.</li>
<li><strong>طلب:</strong> يحدد الترتيب المرئي الذي تظهر به النطاقات في قوائم واجهة المستخدم.</li>
<li><strong>تم الإنشاء في:</strong> الطابع الزمني لوقت تسجيل النطاق.</li>
</ul>
<h2>4. النطاق ↔ علاقة المجال</h2>
<ul>
<li><strong>كيفية ربط النطاقات:</strong> ترتبط النطاقات بشكل صريح بالنطاقات عبر التعيين.</li>
<li><strong>رسم الخرائط:</strong> تقوم العلاقة بوضوح بتعيين نطاق إلى مجال بالإضافة إلى حالته النشطة.</li>
<li><strong>نوع العلاقة:</strong> هذا هو <strong>كثير لكثير</strong> علاقة. يمكن ربط نطاق واحد (مثل "المصادقة") بنطاقات متعددة (مثل كل من "المسؤول" و"العميل")، ويمكن أن يحتوي النطاق الواحد على العديد من النطاقات.</li>
<li><strong>قيود:</strong><ul>
<li>يمكن أن يوجد المجال بشكل مستقل دون أن يتم تعيينه إلى أي نطاق.</li>
<li>يمكن أن يوجد النطاق بدون أي مجالات مخصصة له.</li>
<li>ومع ذلك، قبل أن يتمكن المسؤول من إنشاء مفتاح ترجمة، يتحقق النظام بشكل صارم من أن المجال المحدد مسموح به بشكل صريح وتعيينه للنطاق المحدد.</li>
</ul>
</li>
</ul>
<h2>5. دورة حياة النطاق (سلوك المسؤول)</h2>
<h3>إنشاء النطاق</h3>
<ul>
<li><strong>ما مدخلات المشرف:</strong> يوفر المسؤول رمزًا واسمًا ووصفًا اختياريًا.</li>
<li><strong>ما التحقق من الصحة موجود:</strong> يتحقق النظام من أن الرمز فريد ومنسق بشكل صحيح.</li>
<li><strong>ماذا يحدث في النظام:</strong> تم تسجيل النطاق الجديد بشكل آمن.</li>
</ul>
<h3>تحرير النطاق</h3>
<ul>
<li><strong>ما هي الحقول القابلة للتحرير:</strong> يمكن للمسؤولين تحرير التعليمات البرمجية (من خلال إجراء تغيير التعليمات البرمجية المخصص)، والاسم والوصف (من خلال إجراء تحديث بيانات التعريف)، وترتيب الفرز (من خلال إجراء فرز التحديث).</li>
<li><strong>ما هو مقيد:</strong> إذا حاولت تغيير الرمز، فسيظهر نموذج تحذير يتطلب التأكيد.</li>
</ul>
<h3>تفعيل / إلغاء التنشيط</h3>
<ul>
<li><strong>ماذا تعني كلمة "نشط":</strong> يتم تبديل الحالة النشطة أو إيقاف تشغيلها.</li>
<li><strong>كيف يؤثر على سلوك النظام:</strong> يؤدي إلغاء تنشيط النطاق إلى إخفائه من القوائم القياسية ويمنع حل المفاتيح الجديدة ضمن مساحة الاسم تلك.</li>
</ul>
<h2>6. قائمة النطاق</h2>
<p>تعرض واجهة المستخدم الرئيسية لإدارة النطاقات جدول بيانات بالبنية التالية:</p>
<ul>
<li><strong>أعمدة الجدول:</strong> المعرف والرمز (المقدم بشارة رمز)، والاسم، والوصف، والنشط (يتم تقديمه كشارة حالة نشطة/غير نشطة)، والأمر (شارة ترتيب الفرز)، والإجراءات.</li>
<li><strong>الإجراءات:</strong> يحتوي كل صف على عدة أزرار قابلة للتنفيذ بناءً على أذونات المسؤول:</li>
<li><strong>شفرة:</strong> يفتح نموذجًا لتغيير رمز النطاق البرمجي.</li>
<li><strong>ميتا:</strong> يفتح نموذجًا لتحديث الاسم والوصف.</li>
<li><strong>نوع:</strong> يفتح نموذجًا لتغيير ترتيب الفرز.</li>
<li><strong>التنشيط/التعطيل:</strong> زر تبديل لتغيير الحالة النشطة.</li>
<li><strong>أزرار:</strong> كما يتميز الجدول عالمي <strong>إنشاء النطاق</strong> الزر الموجود في الأعلى لتهيئة نطاق جديد.</li>
</ul>
<h2>7. نطاق تدفق التفاعل</h2>
<ul>
<li><strong>ماذا يحدث عند النقر على النطاق:</strong> يؤدي النقر فوق معرف النطاق في الجدول إلى إعادة توجيه المسؤول إلى صفحة تفاصيل النطاق المحددة.</li>
<li><strong>كيف يؤدي إلى المجالات:</strong> تقوم صفحة "تفاصيل النطاق" بتحميل جدول "تعيينات المجال". يعرض هذا فقط المجالات المعينة لهذا النطاق المحدد.</li>
<li><strong>ما هي البيانات التي يتم تحميلها:</strong> يقوم النظام بتحميل البيانات الوصفية للنطاق، ومجالاته المعينة، وإحصائيات تغطية الترجمة المحسوبة الخاصة به.</li>
</ul>
<h2>8. القيود والقواعد</h2>
<ul>
<li><strong>تفرد الكود:</strong> يجب أن يكون رمز النطاق فريدًا تمامًا على مستوى العالم.</li>
<li><strong>قواعد التسمية:</strong> يجب توفير الاسم، بينما يكون الوصف اختياريًا.</li>
<li><strong>قيود التبعية:</strong> لا يمكنك تعيين مجال إلى نطاق إذا كان هذا المجال غير موجود. لا يمكنك إنشاء مفتاح لمجموعة النطاق+المجال إذا لم يتم تعيين هذه المجموعة ووضع علامة عليها كنشط.</li>
</ul>
<h2>9. التأثير على نظام الترجمات</h2>
<ul>
<li><strong>كيف يؤثر النطاق على المفاتيح:</strong> كل مفتاح يتطلب نطاقًا. إذا تم تغيير رمز النطاق، يقوم النظام تلقائيًا بتحديث جميع إحصائيات تغطية الترجمة المقابلة.</li>
<li><strong>كيف يؤثر النطاق على القيم:</strong> إذا تم إلغاء تنشيط النطاق، فسيتم إخفاء الترجمات المرتبطة به.</li>
<li><strong>تقديم واجهة المستخدم:</strong> يؤدي تغيير اسم النطاق أو ترتيب الفرز إلى تحديث كيفية ظهور قوائم إدارة الترجمة للمسؤولين.</li>
</ul>
<h2>10. سلوك النظام واتساقه</h2>
<ul>
<li><strong>التغييرات الفورية والمتأخرة:</strong> جميع التعديلات على النطاقات (إنشاء البيانات الوصفية وإعادة تسميتها وتحديثها) تكون متزامنة وفورية.</li>
<li><strong>التحقق من الصحة:</strong> يتحقق النظام بشكل صارم من أن أي إجراء يتم تنفيذه على المفتاح يتحقق من أن نطاقه الأصلي ومجاله مسموح بهما.</li>
<li><strong>حالات الخطأ:</strong> إذا حاول المسؤول اتخاذ إجراء غير صالح، فسيقوم النظام بحظر الطلب وعرض خطأ.</li>
</ul>
<h2>11. الحدود</h2>
<ul>
<li><strong>ما يتحكم فيه النطاق:</strong> يتحكم النطاق بشكل صارم في حدود مساحة الاسم ذات المستوى الأعلى لمفاتيح الترجمة. إنها بمثابة حاوية أصل إلزامية تحدد المجالات المسموح باستخدامها داخلها.</li>
<li><strong>ما لا تسيطر عليه:</strong> لا يتحكم النطاق في توفر اللغة أو آليات الرجوع أو قيم النص الفعلية. إنها مساحة اسم تنظيمية بحتة.</li>
</ul>


