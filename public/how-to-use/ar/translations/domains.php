<h1>إدارة مجالات الترجمة</h1>
<h2>1. ما هو المجال</h2>
<p>النطاق هو مستوى التصنيف الثانوي في نظام الترجمة، ويعمل بمثابة مجموعة وظيفية أو مساحة اسم فرعية ضمن نطاق أكبر. وهو يمثل ميزة أو وحدة نمطية أو منطقة منطقية محددة للتطبيق.</p>
<ul>
<li><strong>أمثلة:</strong> تشمل المجالات المشتركة <code>auth</code> (للنصوص المتعلقة بالمصادقة مثل تسجيل الدخول وإعادة تعيين كلمة المرور)، <code>errors</code> (لرسائل خطأ النظام)، <code>emails</code> (لقوالب البريد الإلكتروني)، أو <code>dashboard</code> (لواجهة المستخدم الرئيسية).</li>
</ul>
<h2>2. دور المجالات في الهندسة المعمارية</h2>
<ul>
<li><strong>تَسَلسُل:</strong> تقع المجالات أسفل النطاقات وفوق المفاتيح في التسلسل الهرمي للترجمة: <strong>النطاقات ← المجالات ← المفاتيح ← القيم</strong>.</li>
<li><strong>المتطلبات الإلزامية:</strong> يجب أن ينتمي كل مفتاح ترجمة في النظام إلى مجموعة نطاق ومجال محددة. لا يمكن أن يوجد المفتاح بدون المجال.</li>
<li><strong>التجميع المنطقي:</strong> توفر النطاقات طريقة منظمة لتنظيم الآلاف من مفاتيح الترجمة، مما يسهل إدارتها وتصديرها وتعيينها للمترجمين.</li>
</ul>
<h2>3. نموذج بيانات المجال</h2>
<p>تتكون النطاقات من الخصائص التالية:</p>
<ul>
<li><strong>بطاقة تعريف:</strong> معرف النظام الفريد للمجال.</li>
<li><strong>شفرة:</strong> معرف السلسلة الفريد الآلي المستخدم في النظام (على سبيل المثال، "auth").</li>
<li><strong>اسم:</strong> الاسم الذي يمكن قراءته بواسطة الإنسان والمعروض في واجهة المستخدم.</li>
<li><strong>وصف:</strong> البيانات الوصفية الاختيارية التي تشرح الغرض من المجال.</li>
<li><strong>الحالة النشطة:</strong> علامة تحدد ما إذا كان المجال ممكّنًا ومتاحًا حاليًا لتحليل الترجمة.</li>
<li><strong>طلب:</strong> يحدد الترتيب المرئي الذي تظهر به المجالات في قوائم واجهة المستخدم.</li>
<li><strong>تم الإنشاء في:</strong> الطابع الزمني لوقت تسجيل النطاق.</li>
</ul>
<h2>4. النطاق ↔ علاقة المجال</h2>
<ul>
<li><strong>رسم الخرائط:</strong> يجب تعيين المجال بشكل صريح إلى نطاق واحد أو أكثر لاستخدامه. يحدد هذا التعيين المكان الذي يكون فيه المجال صالحًا.</li>
<li><strong>نوع العلاقة:</strong> هذا هو <strong>كثير لكثير</strong> علاقة. يمكن ربط نطاق واحد (مثل "المصادقة") بنطاقات متعددة (مثل كل من "المسؤول" و"العميل")، ويمكن أن يحتوي النطاق الواحد على العديد من النطاقات.</li>
<li><strong>قيود:</strong><ul>
<li>يمكن إنشاء المجال بشكل مستقل دون أن يتم تعيينه على الفور إلى النطاق.</li>
<li>يتوفر زر "إنشاء مفتاح" فقط عندما يتم تعيين مجال لنطاق ما.</li>
<li>يحدد الجمع بين النطاق والمجال مساحة الاسم لمفاتيح الترجمة.</li>
</ul>
</li>
</ul>
<h2>5. دورة حياة المجال (سلوك المسؤول)</h2>
<h3>إنشاء المجال</h3>
<ul>
<li><strong>ما مدخلات المشرف:</strong> يوفر المسؤول رمزًا واسمًا ووصفًا اختياريًا.</li>
<li><strong>ما التحقق من الصحة موجود:</strong> يتحقق النظام من أن الرمز فريد في جميع المجالات ومن تنسيقه بشكل صحيح.</li>
<li><strong>ماذا يحدث في النظام:</strong> يتم تسجيل النطاق الجديد بشكل آمن ويصبح متاحًا للتعيين على النطاقات.</li>
</ul>
<h3>تحرير المجال</h3>
<ul>
<li><strong>ما هي الحقول القابلة للتحرير:</strong> يمكن للمسؤولين تحرير التعليمات البرمجية (من خلال إجراء تغيير التعليمات البرمجية)، والاسم والوصف (من خلال إجراء تحديث البيانات التعريفية)، وترتيب الفرز (من خلال إجراء فرز التحديث).</li>
<li><strong>ما هو مقيد:</strong> لا يمكن تغيير المعرف الداخلي. يُسمح بتعديل الكود ولكنه يؤثر على أي مفاتيح مرتبطة بهذا المجال عبر جميع النطاقات المعينة.</li>
</ul>
<h3>تفعيل / إلغاء التنشيط</h3>
<ul>
<li><strong>ماذا تعني كلمة "نشط":</strong> يمكن تبديل الحالة النشطة أو إيقاف تشغيلها.</li>
<li><strong>كيف يؤثر على سلوك النظام:</strong> يؤدي إلغاء تنشيط المجال عالميًا إلى منع استخدامه في أي تعيينات جديدة وقد يؤثر على دقة المفاتيح الموجودة اعتمادًا على تكوين النظام.</li>
</ul>
<h2>6. قائمة المجال</h2>
<p>تعرض واجهة المستخدم الرئيسية لإدارة النطاقات جدول بيانات بالبنية التالية:</p>
<ul>
<li><strong>أعمدة الجدول:</strong> المعرف والرمز (المقدم بشارة رمز)، والاسم، والوصف، والنشط (يتم تقديمه كشارة حالة نشطة/غير نشطة)، والأمر (شارة ترتيب الفرز)، والإجراءات.</li>
<li><strong>الإجراءات:</strong> يحتوي كل صف على أزرار قابلة للتنفيذ بناءً على أذونات المسؤول:</li>
<li><strong>شفرة:</strong> يفتح نموذجًا لتغيير رمز المجال البرمجي.</li>
<li><strong>ميتا:</strong> يفتح نموذجًا لتحديث الاسم والوصف.</li>
<li><strong>نوع:</strong> يفتح نموذجًا لتغيير ترتيب الفرز.</li>
<li><strong>التنشيط/التعطيل:</strong> زر تبديل لتغيير الحالة النشطة العالمية للمجال.</li>
<li><strong>أزرار:</strong> يتضمن الجدول عالمي <strong>إنشاء المجال</strong> الزر الموجود في الأعلى لتهيئة مجال جديد.</li>
</ul>
<h2>7. تدفق تفاعل المجال</h2>
<ul>
<li><strong>ماذا يحدث عند النقر على المجال:</strong> يؤدي النقر فوق المجال في الجدول إلى توجيه المسؤول إلى صفحة تفاصيل المجال المحددة.</li>
<li><strong>كيف يؤدي إلى النطاقات:</strong> تعرض صفحة "تفاصيل المجال" جدول "تعيينات النطاق"، موضحًا جميع النطاقات التي تم تعيين هذا النطاق المحدد لها حاليًا.</li>
<li><strong>ما هي البيانات التي يتم تحميلها:</strong> يقوم النظام بتحميل البيانات التعريفية للنطاق ونطاقاته المعينة وإحصائيات تغطية الترجمة المجمعة لهذا النطاق.</li>
</ul>
<h2>8. القيود والقواعد</h2>
<ul>
<li><strong>تفرد الكود:</strong> يجب أن يكون رمز المجال فريدًا عالميًا عبر جميع النطاقات.</li>
<li><strong>قواعد التسمية:</strong> مطلوب اسم صالح، في حين أن الوصف اختياري.</li>
<li><strong>قيود التبعية:</strong> لا يمكنك إنشاء مفتاح لمجال معين ما لم يتم تعيين هذا المجال إلى نطاق ويكون كلاهما نشطًا.</li>
</ul>
<h2>9. التأثير على نظام الترجمات</h2>
<ul>
<li><strong>كيف يؤثر المجال على المفاتيح:</strong> يتطلب كل مفتاح المجال. إذا تم تغيير رمز المجال، يقوم النظام تلقائيًا بتحديث مسار مساحة الاسم لجميع المفاتيح المقابلة.</li>
<li><strong>كيف يؤثر المجال على القيم:</strong> ترتبط القيم (الترجمات) بمعرفات المفاتيح. إذا تم إلغاء تنشيط المجال أو حذفه، فإن البنية الكاملة للمفاتيح والقيم الموجودة ضمنه تصبح غير متاحة لمساحة اسم النطاق هذا.</li>
<li><strong>تقديم واجهة المستخدم:</strong> تعمل التغييرات التي يتم إجراؤها على اسم النطاق أو ترتيب الفرز على الفور على تحديث كيفية ظهور قوائم إدارة الترجمة وعوامل التصفية للمسؤولين.</li>
</ul>
<h2>10. سلوك النظام واتساقه</h2>
<ul>
<li><strong>التغييرات الفورية والمتأخرة:</strong> جميع التعديلات على النطاقات (إنشاء البيانات الوصفية وإعادة تسميتها وتحديثها) تكون متزامنة وتنعكس فورًا في النظام.</li>
<li><strong>التحقق من الصحة:</strong> يتحقق النظام بشكل صارم من صحة جميع الإجراءات التي يتم تنفيذها على المجال، مما يضمن التعامل مع التعيينات والمفاتيح التابعة بشكل صحيح.</li>
<li><strong>حالات الخطأ:</strong> سيتم حظر محاولة إجراء غير صالح، مثل إنشاء مجال برمز مكرر أو حذف نطاق لا يزال يحتوي على مفاتيح نشطة، من قبل النظام مع ظهور رسالة خطأ.</li>
</ul>
<h2>11. الحدود</h2>
<ul>
<li><strong>ما يتحكم فيه المجال:</strong> يتحكم المجال بشكل صارم في حدود مساحة الاسم الثانوية لمفاتيح الترجمة. إنه بمثابة حاوية وظيفية داخل النطاق.</li>
<li><strong>ما لا تسيطر عليه:</strong> لا يتحكم المجال في توفر اللغة أو آليات الرجوع أو قيم النص المترجم الفعلي. إنه تصنيف تنظيمي بحت.</li>
</ul>


