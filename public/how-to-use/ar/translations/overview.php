<h2>9. حدود هذه الوحدة</h2>
<ul>
<li><strong>ماذا تفعل هذه الوحدة:</strong> فهو يدير بشكل صارم تسجيل مفاتيح النص الصالحة، ويفرض التسلسل الهرمي للنطاق والمجال، ويخزن ويستعيد السلاسل النصية المترجمة الفعلية. ويحتفظ بإحصائيات حول تغطية الترجمة.</li>
<li><strong>ما لا يفعله:</strong> ولا يدير هوية اللغة أو رموز اللغة أو التكوينات الاحتياطية. تنتمي هذه المسؤوليات بالكامل إلى وحدة اللغات.</li>
</ul>


