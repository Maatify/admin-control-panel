<h1>إدارة الترجمات — نظرة عامة على النظام</h1>
<h2>1. الغرض من نظام الترجمات</h2>
<p>نظام الترجمات عبارة عن طبقة ترجمة مصممة لإدارة كل النصوص المترجمة عبر النظام الأساسي. فهو يحل مشكلة ترميز السلاسل الثابتة في التطبيقات من خلال السماح للمسؤولين بتعريف النص وتحريره وتنظيمه ديناميكيًا بلغات متعددة. يستخدم المسؤولون هذا النظام للتحكم بالضبط في ما يقرأه المستخدمون في الواجهة، مما يضمن مصطلحات متسقة ويسمح للنظام الأساسي بخدمة جمهور عالمي دون الحاجة إلى نشر التعليمات البرمجية لتغييرات النص.</p>
<h2>2. البنية الأساسية</h2>
<p>ينظم النظام الترجمات في تسلسل هرمي من أربعة مستويات:</p>
<ul>
<li><strong>النطاقات:</strong> مساحة الاسم ذات المستوى الأعلى (على سبيل المثال، admin، client، api). وهو يمثل حدود التطبيق الرئيسية. يتصل بالمجالات لتنظيم ميزات محددة.</li>
<li><strong>المجالات:</strong> مجموعة فرعية ضمن النطاق (على سبيل المثال، المصادقة، المنتجات، الأخطاء). إنه يمثل ميزة أو وحدة محددة. يجب السماح بالمجال بشكل صريح للنطاق قبل أن يتم إنشاء المفاتيح ضمنه.</li>
<li><strong>المفاتيح:</strong> السجل المنظم لمعرفات النص الصالحة (على سبيل المثال، تسجيل الدخول. العنوان). المفتاح هو مزيج صارم من النطاق والمجال والجزء الرئيسي. إنه بمثابة مرساة لقيم النص الفعلية.</li>
<li><strong>قيم:</strong> السلاسل النصية المترجمة الفعلية. ترتبط كل قيمة مباشرة بمفتاح ولغة محددين.</li>
</ul>
<h2>3. كيف تعمل الترجمات في النظام</h2>
<p>عندما يعرض النظام نصًا في واجهة المستخدم، فإنه يتبع القواعد التالية:</p>
<ol>
<li><strong>دقة اللغة:</strong> يحدد النظام اللغة المطلوبة للمستخدم.</li>
<li><strong>بحث النظام:</strong> يقوم النظام باسترداد الترجمة الدقيقة المطابقة للغة والمفتاح المطلوبين.</li>
<li><strong>السلوك الاحتياطي:</strong> إذا كانت الترجمة الدقيقة مفقودة، يستخدم النظام سلسلة احتياطية صارمة. يتحقق مما إذا كانت اللغة المطلوبة تحتوي على لغة احتياطية محددة. إذا كان الأمر كذلك، فإنه يحاول تلقائيًا تحميل الترجمة لتلك اللغة الاحتياطية. إذا كانت كل من اللغات الأساسية والاحتياطية تفتقد الترجمة، فسيقوم النظام بإرجاع سلسلة المفاتيح الأولية.</li>
</ol>
<h2>4. نموذج التفاعل الإداري</h2>
<p>يتفاعل المسؤولون مع النظام من خلال تدفق تفصيلي مصمم لفرض التسلسل الهرمي الصارم:</p>
<ol>
<li><strong>ابدأ بالنطاقات:</strong> يبدأ المسؤول من المستوى الأعلى، ويعرض مساحات الأسماء المتاحة.</li>
<li><strong>انتقل لأسفل إلى المجالات:</strong> يحدد المسؤول نطاقًا لعرض النطاقات المخصصة له بشكل صريح.</li>
<li><strong>انتقل لأسفل إلى المفاتيح:</strong> يحدد المسؤول مجالًا لعرض سجل المفاتيح التي تنتمي إلى زوج النطاق والمجال المحدد.</li>
<li><strong>التحرير الفعلي:</strong> يصل المسؤول إلى المستوى الأعمق (قائمة الترجمات) حيث يقوم بإدارة القيم النصية الفعلية المرتبطة بتلك المفاتيح عبر اللغات المختلفة.</li>
</ol>
<h2>5. نموذج تحرير الترجمة</h2>
<ul>
<li><strong>كيف يتم تحرير القيم:</strong> يقوم المسؤولون بتحرير القيم مباشرة من خلال واجهة الترجمة.</li>
<li><strong>كيف يتم التعامل مع اللغات المتعددة:</strong> يتم إرفاق الترجمات للغات متعددة بنفس المفتاح.</li>
<li><strong>كيفية نشر التحديثات إلى النظام:</strong> يستخدم النظام نموذج اتساق صارم. لا توجد تأخيرات في الخلفية. عندما يتم تحديث الترجمة، يتم تطبيقها على الفور. وفي الوقت نفسه، تتم إعادة حساب جميع إحصائيات تغطية الترجمة على الفور للتأكد من أن لوحات التحكم الإدارية تعكس التغطية الجديدة على الفور.</li>
</ul>
<h2>6. التبعية اللغوية</h2>
<p>تعتمد وحدة الترجمات على اللغات النشطة المحددة في النظام.</p>
<ul>
<li><strong>كيف تؤثر اللغات على الترجمات:</strong> لا تحدد وحدة الترجمات اللغات الموجودة. يعتمد بشكل صارم على قائمة اللغات النشطة. يجب أن تكون كل ترجمة مرفقة بلغة صالحة.</li>
<li><strong>ماذا يحدث عندما تكون اللغة غير نشطة/مفقودة:</strong> إذا تم حذف لغة ما أو إلغاء تنشيطها، تصبح الترجمات المرتبطة بتلك اللغة غير متاحة. سيقوم النظام بحظر أي محاولات لحفظ الترجمة للغة غير صالحة.</li>
<li><strong>كيف يتم استخدام اللغة الاحتياطية:</strong> يتم تعريف اللغة الاحتياطية في قسم إدارة اللغات. تقوم وحدة الترجمات ببساطة بقراءة هذا التكوين الاحتياطي أثناء وقت التشغيل لحل المفاتيح المفقودة.</li>
</ul>
<h2>7. سلوك النظام واتساقه</h2>
<ul>
<li><strong>التحديثات الفورية والمتأخرة:</strong> جميع التحديثات متسقة بقوة وفورية. يفرض النظام تحديثات فورية.</li>
<li><strong>التخزين المؤقت:</strong> يتم تحديث الترجمات على الفور دون الحاجة إلى مسح ذاكرة التخزين المؤقت.</li>
<li><strong>تأثير تغيير المفاتيح المشتركة:</strong> نظرًا لأن المفاتيح مسجلة بشكل صارم ويتم الاستعلام عنها عالميًا بواسطة التطبيق، فإن تغيير قيمة المفتاح المشترك (على سبيل المثال، تغيير نص زر "حفظ" عام) سيؤدي على الفور وبشكل فوري إلى تحديث هذا النص في كل مكان تتم الإشارة فيه إلى المفتاح عبر النظام الأساسي بأكمله.</li>
</ul>
