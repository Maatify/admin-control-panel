<h1>إدارة الأدوار</h1>
<h2>ملخص</h2>
<p>تتحكم الأدوار في الوصول عبر النظام الأساسي. يقوم الدور بتجميع أذونات محددة معًا حتى تتمكن من منح مجموعة قياسية من حقوق الوصول إلى مسؤول بمهمة واحدة، بدلاً من إدارة مئات الأذونات المحددة بشكل فردي.</p>
<h2>كيفية الوصول إلى الأدوار</h2>
<p>لإدارة الأدوار:
1. انظر إلى قائمة التنقل في الشريط الجانبي الأيسر.
2. افتح <strong>RBAC</strong> قسم.
3. انقر على <strong>الأدوار</strong>. يؤدي هذا إلى فتح صفحة قائمة الأدوار الرئيسية.</p>
<h2>قائمة الأدوار</h2>
<p>عندما تفتح قسم الأدوار، سترى جدولاً بجميع الأدوار الموجودة.</p>
<ul>
<li><strong>أعمدة الجدول:</strong> يعرض الجدول اسم كل دور وحالته الحالية.</li>
<li><strong>الإجراءات:</strong> يمكنك النقر فوق <strong>منظر</strong> أو <strong>يحرر</strong> الأزرار الموجودة بجوار الدور الحالي، أو انقر فوق <strong>إنشاء دور</strong> زر لإضافة واحدة جديدة.</li>
<li><strong>المرشحات والبحث:</strong> يوجد أعلى الجدول شريط بحث عالمي للعثور على الأدوار على الفور حسب الاسم أو المجموعة. يمكنك أيضًا النقر فوق شارات التصفية السريعة لعرض الأدوار "الكل" أو "النشطة" أو "غير النشطة". بالإضافة إلى ذلك، يسمح لك نموذج التصفية المخصص بالبحث على وجه التحديد حسب معرف الدور أو الاسم أو المجموعة، مع استكمال زر إعادة الضبط لمسح عوامل التصفية النشطة.</li>
</ul>
<h2>إنشاء دور (التدفق الكامل)</h2>
<p>عندما تحتاج إلى تحديد وظيفة وظيفة جديدة بحقوق وصول محددة، اتبع الخطوات التالية:</p>
<ol>
<li>انقر فوق <strong>إنشاء دور</strong> زر.</li>
<li>أدخل واضحا ووصفيا <strong>اسم الدور</strong> (على سبيل المثال، "وكيل الدعم" أو "المترجم").</li>
<li>انقر فوق <strong>إنشاء دور</strong> زر لإنهاء الإنشاء.</li>
<li><strong>نتيجة:</strong> تقوم خطوة الإنشاء فقط بإنشاء المعلومات الأساسية للدور (اسمه). لا يدير الأذونات بشكل كامل من هذه الخطوة. بعد الإنشاء، سيظهر الدور الجديد على الفور في قائمة الأدوار.</li>
</ol>
<h2>عرض وإدارة الدور</h2>
<p>الطريقة الأساسية لتكوين حقوق الوصول والتعيينات الخاصة بالدور هي من خلال شاشة الإدارة الخاصة به.</p>
<ol>
<li>من قائمة الأدوار، انقر فوق <strong>منظر</strong> الزر بجوار الدور.</li>
<li>يؤدي هذا إلى فتح صفحة تفاصيل الدور.</li>
<li>تحتوي هذه الصفحة على قسمين رئيسيين:</li>
<li><strong>علامة تبويب الأذونات:</strong> حيث يمكنك التحكم في ما يسمح به الدور.</li>
<li><strong>علامة التبويب "المسؤولون":</strong> حيث يمكنك التحكم في من يحمل الدور.</li>
</ol>
<h3>إدارة الأذونات</h3>
<p>تتم إدارة الأذونات بشكل صارم من <strong>علامة تبويب الأذونات</strong>.
1. افتح <strong>علامة تبويب الأذونات</strong> في صفحة تفاصيل الدور.
2. تظهر قائمة الأذونات.
3. يحتوي كل إذن على مفتاح تبديل لتمكينه أو تعطيله.
4. يؤدي النقر فوق مفتاح التبديل على الفور إلى تمكين هذا الإذن للدور أو تعطيله.</p>
<ul>
<li><strong>نتيجة:</strong> يؤدي تغيير التبديل على الفور إلى تحديث حقوق الوصول للدور عبر النظام الأساسي بأكمله.</li>
</ul>
<h3>تعيين المسؤول من صفحة الدور</h3>
<p>أنت تدير من يشغل هذا الدور المحدد من <strong>علامة تبويب المسؤولين</strong>.
1. افتح <strong>علامة تبويب المسؤولين</strong> في صفحة تفاصيل الدور.
2. تعرض علامة التبويب قائمة المسؤولين.
3. لدى كل مسؤول زر تبديل بجوار اسمه.
4. يؤدي النقر فوق مفتاح تبديل المسؤول إلى تعيين هذا الدور أو إلغاء تعيينه له على الفور.</p>
<h2>تحرير الدور</h2>
<p>إذا كنت بحاجة فقط إلى تغيير التفاصيل الأساسية للدور:</p>
<ol>
<li>من قائمة الأدوار، انقر فوق <strong>يحرر</strong> الزر بجوار الدور.</li>
<li>يمكنك تعديل معلوماته الأساسية، مثل اسمه.</li>
<li>انقر فوق <strong>يحفظ</strong> زر لتطبيق التغييرات.</li>
</ol>
<h2>ماذا يحدث عندما يتغير الدور</h2>
<p>ونظرًا لأن الأدوار مرتبطة مباشرةً بجلسة المسؤول، فإن أي تعديلات تصبح سارية المفعول على الفور.</p>
<ul>
<li><strong>تحديثات الشريط الجانبي:</strong> إذا قمت بإضافة أو إزالة الأذونات التي تتحكم في أقسام كاملة من النظام الأساسي (مثل قائمة "الإعدادات")، فسيظهر هذا الرابط على الفور أو يختفي من الشريط الجانبي الأيسر لأي مسؤول يشغل هذا الدور.</li>
<li><strong>تظهر/تختفي الأزرار:</strong> إذا قمت بإضافة أو إزالة أذونات إجراء محددة (مثل "إنشاء مسؤول")، فسيصبح هذا الزر المحدد مرئيًا على الفور أو سيتم إخفاؤه على الصفحات ذات الصلة.</li>
<li>إذا كان المسؤول يعرض بشكل نشط صفحة قام تغيير الدور للتو بإلغاء الوصول إليها، فسيقوم النظام بحظره في المرة التالية التي ينقر فيها على زر أو يقوم بتحديث الصفحة.</li>
</ul>
<h2>ملاحظات هامة</h2>
<ul>
<li>تأكد دائمًا من أن الأدوار تتوافق مع مسؤوليات الوظيفة الفعلية.</li>
<li>تجنب المبالغة في تعقيد الأدوار؛ اجعلها واسعة بما يكفي لتكون مفيدة للعديد من الموظفين الذين لديهم واجبات مماثلة.</li>
<li>فضل تعيين الأدوار بدلاً من منح الأذونات المباشرة كلما أمكن ذلك، حيث إن تتبع الأدوار وتدقيقها وإدارتها على نطاق واسع أسهل بكثير.</li>
</ul>


