<h1>إدارة الأذونات</h1>
<h2>ملخص</h2>
<p>الأذونات هي حقوق الوصول المحددة التي تتحكم بالضبط في ما يمكن للمسؤول رؤيته والقيام به على النظام الأساسي. بدلاً من التعامل مع مفاهيم واسعة، تحدد الأذونات الواقع العملي للنظام: فهي تحدد بالضبط الأزرار التي تظهر على الصفحة، والروابط التي تظهر في قائمة التنقل، والصفحات التي يتم تحميلها بنجاح.</p>
<h2>حيث تتم إدارة الأذونات</h2>
<p>لا يتم إنشاء الأذونات أو إدارتها من صفحة مستقلة منفصلة. وبدلاً من ذلك، تتم إدارتها مباشرةً من داخل دور موجود.</p>
<p>لإدارة الأذونات، اتبع الخطوات التالية بالضبط:
1. في الشريط الجانبي الأيسر، انتقل إلى <strong>RBAC</strong> → <strong>الأدوار</strong>.
2. انقر فوق <strong>منظر</strong> الزر بجوار الدور الذي تريد إدارته.
3. في صفحة تفاصيل الدور، انقر فوق <strong>علامة تبويب الأذونات</strong>.</p>
<h2>قائمة الأذونات</h2>
<p>عند فتح علامة التبويب "الأذونات"، سترى قائمة بحقوق الوصول المتوفرة في النظام.</p>
<ul>
<li><strong>أعمدة الجدول:</strong></li>
<li><strong>اسم الإذن:</strong> العنوان المحدد لحق الوصول.</li>
<li><strong>مجموعة:</strong> الفئة التي ينتمي إليها الإذن.</li>
<li><strong>وصف:</strong> شرح لما يسمح الإذن للمستخدم القيام به.</li>
<li><strong>التبديل المخصص:</strong> مفتاح يستخدم لتمكين أو تعطيل الإذن.</li>
<li><strong>المرشحات والبحث:</strong> ستجد أعلى الجدول شريط بحث عالمي للعثور بسرعة على الأذونات عن طريق كتابة أي كلمة رئيسية (يتم البحث تلقائيًا بعد فترة تأخير قصيرة). بالإضافة إلى ذلك، يسمح لك نموذج التصفية بالبحث على وجه التحديد حسب معرف الإذن أو الاسم أو المجموعة. يقوم زر إعادة الضبط بمسح جميع المرشحات النشطة.</li>
</ul>
<h2>تمكين أو تعطيل إذن</h2>
<p>لمنح حق وصول محدد لدور ما أو إزالته، اتبع الخطوات التالية:</p>
<ol>
<li>افتح دورًا باستخدام <strong>منظر</strong> زر من قائمة الأدوار.</li>
<li>اذهب الى <strong>علامة تبويب الأذونات</strong>.</li>
<li>حدد الإذن المحدد الذي تريد تعديله.</li>
<li>انقر فوق <strong>التبديل المعين</strong> بجوار الإذن لتشغيله (ممكّن) أو إيقاف تشغيله (معطل).</li>
</ol>
<p><strong>ماذا يحدث مباشرة بعد التبديل:</strong>
يتم تطبيق التغيير على النظام في اللحظة التي تنقر فيها على زر التبديل. لا يوجد زر "حفظ" مطلوب في علامة التبويب هذه. أي مسؤول يشغل هذا الدور حاليًا سيتم تحديث وصوله على الفور عبر النظام الأساسي.</p>
<h2>كيف تؤثر الأذونات على النظام</h2>
<p>عند تشغيل الإذن أو إيقافه، فإنه يغير واجهة النظام وحدود الأمان مباشرة:
* <strong>تظهر/تختفي أقسام الشريط الجانبي:</strong> إذا كان هناك إذن يتحكم في الوصول إلى قسم بأكمله، فسيظهر الرابط المقابل في الشريط الجانبي الأيسر أو يختفي على الفور.
* <strong>تظهر/تختفي الأزرار:</strong> إذا كان الإذن يتحكم في إجراء معين (مثل حذف سجل)، فسيصبح الزر "حذف" مرئيًا على الفور أو يخفي نفسه في الصفحة ذات الصلة.
* <strong>تم حظر الوصول إلى الصفحات:</strong> إذا حاول أحد المسؤولين زيارة صفحة تم تعطيل إذنها للتو، فسيقوم النظام بحظره على الفور وعرض خطأ.</p>
<h2>مجموعات الأذونات</h2>
<p>يتم تنظيم الأذونات الموجودة في القائمة بشكل منطقي حسب تصنيف "المجموعة" (على سبيل المثال، فصل أذونات إدارة المستخدم عن أذونات إعدادات النظام). وينعكس هذا في عمود "المجموعة" بجدول البيانات، مما يتيح لك فرز حقوق الوصول ذات الصلة وتصفيتها وتحديدها بشكل مرئي بسهولة.</p>
<h2>أمثلة الاستخدام الحقيقي</h2>
<ul>
<li><strong>تمكين "إنشاء مسؤول":</strong> إذا قمت بتشغيل هذا الإذن لأحد الأدوار، فسيرى المسؤولون الذين لديهم هذا الدور على الفور زر "إضافة مسؤول" يظهر في صفحة قائمة المسؤولين.</li>
<li><strong>تمكين "عرض سجلات الأنشطة":</strong> إذا قمت بتشغيل هذا الإذن، فسيظهر رابط "سجلات الأنشطة" على الفور في الشريط الجانبي الأيسر للمسؤول، مما يسمح له بالنقر عليه وعرض الصفحة.</li>
<li><strong>تعطيل الوصول إلى "الإعدادات":</strong> إذا قمت بإيقاف تشغيل أذونات الإعدادات، فسيختفي رابط "الإعدادات" على الفور من الشريط الجانبي. إذا حاول المسؤول كتابة عنوان الويب يدويًا للوصول إلى صفحة الإعدادات، فسيقوم النظام بحظر وصوله بشكل فعال.</li>
</ul>
<h2>ملاحظات هامة</h2>
<ul>
<li><strong>تنطبق التغييرات على الفور:</strong> في اللحظة التي تنقر فيها على زر التبديل، يتم تحديث حقوق الوصول. ليس هناك تأخير.</li>
<li><strong>يجب إدارة الأذونات بعناية:</strong> قم بتمكين التبديل فقط للإجراءات التي يتطلبها الدور المحدد لأداء وظيفته.</li>
<li><strong>يمكن أن تؤدي إزالة الإذن إلى حظر الوصول على الفور:</strong> إذا كان أحد المسؤولين يعمل حاليًا على إحدى الصفحات وقمت بإيقاف تشغيل إذنه، فسيتم حظره في المرة التالية التي ينقر فيها على زر أو يقوم بتحديث شاشته.</li>
</ul>


