<h1>إدارة المشرفين</h1>
<h2>ملخص</h2>
<p>المسؤولون هم الموظفون المعتمدون الذين يقومون بإدارة وتشغيل المنصة. نظرًا لأن لديهم ضوابط واسعة النطاق وإمكانية الوصول إلى المعلومات الحساسة، يجب أن تخضع حساباتهم لرقابة صارمة. يسمح لك هذا القسم بالإشراف على الأشخاص الذين لديهم حق الوصول إلى اللوحة الإدارية، وإدارة تفاصيل حساباتهم، ومراقبة أمانهم وجلسات تسجيل الدخول النشطة الخاصة بهم.</p>
<hr/>
<h2>كيفية الوصول إلى المسؤولين</h2>
<p>للوصول إلى منطقة إدارة المسؤول:</p>
<ul>
<li>انظر إلى قائمة التنقل في الشريط الجانبي الأيسر.</li>
<li>انقر على <strong>المشرفين</strong>. سيؤدي هذا إلى فتح صفحة قائمة المسؤولين الرئيسية.</li>
</ul>
<hr/>
<h2>قائمة المشرفين</h2>
<p>عند فتح قسم "المسؤولين"، سترى <strong>قائمة المشرفين</strong>.</p>
<ul>
<li><strong>أعمدة الجدول:</strong> جدول يعرض جميع حسابات المسؤولين، ويعرض التفاصيل الأساسية مثل الاسم والبريد الإلكتروني الأساسي وحالة الحساب الحالية (على سبيل المثال، نشط أو معطل).</li>
<li><strong>المرشحات والبحث:</strong> يمكنك تصفية القائمة للعثور بسرعة على حسابات محددة، مثل عرض المسؤولين النشطين فقط أو البحث حسب الاسم والبريد الإلكتروني.</li>
<li><strong>الإجراءات:</strong> من هذه القائمة، يمكنك النقر <strong>منظر</strong> بجوار المسؤول لفتح ملفه الشخصي الكامل، أو انقر على <strong>إضافة المشرف</strong> زر لإنشاء حساب جديد.</li>
</ul>
<hr/>
<h2>إنشاء مسؤول جديد (التدفق الكامل)</h2>
<p>عندما تحتاج إلى منح حق الوصول لموظف جديد، يجب عليك اتباع هذا التسلسل الدقيق:</p>
<ol>
<li><strong>تعبئة النموذج:</strong> انقر فوق <strong>إضافة المشرف</strong> زر وأدخل التفاصيل المطلوبة، مثل الاسم الكامل للمسؤول الجديد وعنوان بريده الإلكتروني الرئيسي.</li>
<li><strong>النقر على إنشاء:</strong> أرسل النموذج لإنشاء الحساب.</li>
<li><strong>ماذا سيحدث بعد ذلك:</strong> يتم إنشاء الحساب على الفور، ويتم نقلك تلقائيًا إلى شاشة بيانات الاعتماد الآمنة.</li>
<li><strong>شاشة كلمة المرور المؤقتة:</strong> سيعرض النظام كلمة المرور الأولية للمسؤول الجديد على الشاشة.</li>
<li><strong>الإجراءات المطلوبة:</strong> يجب عليك نسخ كلمة المرور هذه بشكل آمن. يمكنك ببساطة النقر فوق حقل كلمة المرور لنسخها إلى الحافظة الخاصة بك. للمتابعة ومغادرة هذه الشاشة، يتعين عليك التأكيد صراحةً على أنك قمت بتأمين بيانات الاعتماد من خلال إقرار المطالبة ("لقد قمت بحفظ كلمة المرور"). لا يمكنك المتابعة حتى يتم هذا التأكيد.</li>
</ol>
<hr/>
<h2>⚠️ كلمة المرور المؤقتة (القسم النقدي)</h2>
<p>عند إنشاء مسؤول جديد، يتعامل النظام مع وصوله الأولي بموجب قواعد أمنية صارمة:</p>
<ul>
<li><strong>يتم إنشاء كلمة المرور تلقائيًا:</strong> يقوم النظام بإنشاء كلمة مرور مؤقتة وعشوائية آمنة للغاية. لا يمكنك اختياره.</li>
<li><strong>يتم عرضه مرة واحدة فقط:</strong> تظهر كلمة المرور على الشاشة فور تقديم نموذج إنشاء الحساب.</li>
<li><strong>يجب عليك نسخه على الفور:</strong> أنت مسؤول عن نسخ كلمة المرور هذه (بالنقر فوق حقل كلمة المرور) وتقديمها بشكل آمن إلى المسؤول الجديد.</li>
<li><strong>يتطلب النظام تأكيدًا:</strong> لا يمكنك الإغلاق أو الابتعاد عن طريق الخطأ دون النقر لتأكيد "لقد قمت بحفظ كلمة المرور". يقوم النظام بحظر كل التنقلات حتى يتم ذلك.</li>
<li><strong>لن يتم عرضه مرة أخرى أبدًا:</strong> بمجرد مغادرة تلك الشاشة، تختفي كلمة المرور إلى الأبد. إذا فقدت قبل أن يقوم المسؤول الجديد بتسجيل الدخول، فسيتعين عليك إعادة تعيين بيانات الاعتماد الخاصة به بالكامل.</li>
</ul>
<hr/>
<h2>الملف الشخصي للمسؤول</h2>
<p>النقر <strong>منظر</strong> بجوار المسؤول في القائمة يفتح كاملا <strong>الملف الشخصي للمسؤول</strong>. تم تنظيم الصفحة إلى مناطق متميزة:</p>
<ul>
<li><strong>معلومات الملف الشخصي:</strong> في الأعلى، سترى معلوماتهم العامة، مثل الاسم وحالة الحساب الحالي.</li>
<li><strong>الأقسام:</strong> أسفل المعلومات العامة، ينقسم الملف الشخصي إلى أقسام محددة:</li>
<li><strong>رسائل البريد الإلكتروني:</strong> لإدارة جميع عناوين البريد الإلكتروني المرتبطة بالمسؤول.</li>
<li><strong>الجلسات:</strong> لمراقبة والتحكم في مكان تسجيل دخول المسؤول حاليًا.</li>
<li><strong>إشعارات:</strong> لمراجعة تاريخ تنبيهات النظام والرسائل المرسلة إلى هذا المشرف.</li>
</ul>
<hr/>
<h2>إدارة رسائل البريد الإلكتروني</h2>
<p>يمكن أن يكون لدى المسؤول عناوين بريد إلكتروني متعددة مرتبطة بحسابه.</p>
<ul>
<li><strong>رسائل البريد الإلكتروني المتعددة المسموح بها:</strong> يمكنك إضافة رسائل بريد إلكتروني احتياطية أو ثانوية إلى الحساب.</li>
<li><strong>الحالات:</strong> سيعرض كل عنوان بريد إلكتروني إحدى الحالات التالية:</li>
<li><strong>قيد الانتظار:</strong> تمت إضافة البريد الإلكتروني ولكن المالك لم ينقر بعد على رابط التحقق. <em>إذا كانت رسالة البريد الإلكتروني "معلقة"، فسيعرض النظام شارة "معلقة". سترى أزرار الإجراءات "للتحقق يدويًا" أو "وضع علامة فشل".</em></li>
<li><strong>تم التحقق:</strong> تم تأكيد البريد الإلكتروني ونشط وقابل للاستخدام بالكامل.</li>
<li><strong>فشل:</strong> لم تكن عملية التحقق ناجحة أو انتهت صلاحيتها.</li>
<li><strong>تم الاستبدال:</strong> بريد إلكتروني قديم تم استبداله بعنوان أساسي جديد.</li>
<li><strong>يمكن استخدام رسائل البريد الإلكتروني التي تم التحقق منها فقط:</strong> لا يمكن للمسؤول استخدام البريد الإلكتروني لتسجيل الدخول أو تلقي إشعارات النظام الهامة حتى يتم تحديد حالته <strong>تم التحقق منه</strong>.</li>
<li><strong>الإجراءات:</strong> من قسم رسائل البريد الإلكتروني، يمكنك ذلك <strong>أضف البريد الإلكتروني</strong> لإرسال طلب تحقق جديد، <strong>التحقق يدويا</strong> إذا كانت لديك سلطة إدارية لتجاوز التحقق من البريد الإلكتروني، أو <strong>وضع علامة فشل</strong> لإبطال الطلب المعلق.</li>
</ul>
<hr/>
<h2>إدارة الجلسات</h2>
<p>تمثل "الجلسة" تسجيل دخول نشطًا على جهاز كمبيوتر أو هاتف أو متصفح معين.</p>
<ul>
<li><strong>قائمة الجلسات:</strong> يعرض هذا القسم سجلاً تفصيليًا لكل مكان يتواجد فيه المسؤول حاليًا أو تم تسجيل دخوله مسبقًا.</li>
<li><strong>المرشحات والبحث:</strong> يمكنك فرز الجلسات باستخدام المرشحات: <strong>الجميع</strong>, <strong>نشيط</strong> (تم تسجيل الدخول حاليا)، <strong>منتهي الصلاحية</strong> (انتهت المهلة بشكل طبيعي)، أو <strong>تم إبطاله</strong> (أجبر على الخروج).</li>
<li><strong>إشارة الجلسة الحالية:</strong> النظام يسلط الضوء بوضوح على الخاص بك <em>الجلسة الحالية</em> حتى لا تقوم بتسجيل الخروج عن طريق الخطأ أثناء مراجعة القائمة.</li>
<li><strong>إبطال الإجراءات:</strong> إذا رأيت جهازًا قديمًا أو غير معروف، فيمكنك النقر فوقه <strong>إبطال</strong> بجانبه لتسجيل الخروج من هذا الجهاز على الفور. لديك أيضًا إمكانية الوصول إلى أ <strong>إبطال بالجملة</strong> إجراء لتسجيل خروج المسؤول على الفور من جميع الجلسات النشطة مرة واحدة.</li>
</ul>
<hr/>
<h2>مشرف التحرير</h2>
<p>إذا تغيرت تفاصيل المسؤول أو غادر المؤسسة، فيمكنك تحرير ملفه الشخصي الأساسي.</p>
<ul>
<li><strong>تعديل الاسم والحالة:</strong> يمكنك تحديث اسمه بالكامل وتغيير حالة حسابه (على سبيل المثال، من نشط إلى معطل).</li>
<li><strong>تؤثر الحالة على الوصول:</strong> يؤدي تعطيل الحساب على الفور إلى منع ذلك المسؤول من تسجيل الدخول إلى النظام الأساسي أو اتخاذ أي إجراءات أخرى.</li>
<li><strong>يتم تدقيق التغييرات:</strong> يتم تسجيل كل تعديل يتم إجراؤه على الملف الشخصي للمسؤول بشكل صارم في سجلات تدقيق النظام، مع توضيح من قام بالتغيير ومتى.</li>
</ul>
<hr/>
<h2>ملاحظات أمنية</h2>
<ul>
<li><strong>يجب التحقق من البريد الإلكتروني:</strong> لا يوجد عنوان بريد إلكتروني جديد يمنح الوصول إلى النظام الأساسي إلا بعد اجتياز عملية التحقق الصارمة.</li>
<li><strong>كلمة المرور المؤقتة تستخدم لمرة واحدة فقط:</strong> كن مستعدًا دائمًا لنسخ كلمة المرور المؤقتة أثناء إنشاء الحساب، حيث لا يتم عرضها مرتين أبدًا.</li>
<li><strong>يجب إلغاء الجلسات في حالة الشك:</strong> إذا أبلغ أحد المسؤولين عن فقدان جهاز أو لاحظت موقعًا غير معتاد لتسجيل الدخول، فاستخدم أدوات الإلغاء على الفور لحماية النظام الأساسي.</li>
<li><strong>يتم تتبع الإجراءات:</strong> يتم تتبع كل إجراء، بدءًا من إنشاء الحساب وحتى إلغاء الجلسة، بشكل دائم في سجلات تدقيق النظام لتحقيق المساءلة الكاملة.</li>
</ul>


