<h1>إدارة العملات</h1>
<h2>نظرة عامة</h2>
<p>تتيح لك صفحة العملات عرض وإدارة قائمة العملات النقدية المتاحة في المنصة. يمكنك رؤية تفاصيل كل عملة وتعديل كيفية عرض أسمائها باللغات المختلفة.</p>

<h2>كيفية الوصول إلى العملات</h2>
<ol>
<li>حدد موقع قائمة التنقل الجانبية اليسرى.</li>
<li>انقر على رابط <strong>العملات</strong>.</li>
<li>ستفتح الصفحة الرئيسية لقائمة العملات.</li>
</ol>

<h2>قائمة العملات</h2>
<p>تعرض الواجهة الرئيسية جدولاً بجميع العملات المتاحة.</p>
<ul>
<li><strong>معلومات الجدول:</strong> يعرض الجدول التفاصيل القياسية التي تحدد كل عملة، مثل اسمها (على سبيل المثال، "دولار أمريكي")، والرمز (مثل "USD")، والرمز الرياضي (مثل "$")، وحالة النشاط الحالية.</li>
<li><strong>الإجراءات المتاحة:</strong> يوفر الجدول خيارات للتفاعل مع كل عملة، بما في ذلك القدرة على إدارة كيفية ترجمة اسم العملة.</li>
</ul>

<h2>عرض ترجمات العملات</h2>
<p>لتعديل كيفية ظهور العملة بلغات مختلفة:</p>
<ol>
<li>حدد موقع العملة المحددة في جدول قائمة العملات.</li>
<li>انقر على العملة أو استخدم الإجراء المتاح لفتح ترجماتها.</li>
<li>سينقلك هذا إلى صفحة مخصصة تعرض الترجمات لتلك العملة المحددة.</li>
</ol>

<h2>ترجمات العملات</h2>
<p>يمكن أن يكون لكل عملة ترجماتها الخاصة، مما يضمن أن يرى المستخدمون أسماء عملات مألوفة بلغتهم المحددة.</p>
<ul>
<li><strong>ما يمكنك تعديله:</strong> يمكنك إدارة النص المترجم المرتبط بالعملة، مثل اسم العرض الخاص بها بمختلف اللغات.</li>
<li><strong>نظام الترجمة:</strong> تتبع ترجمات العملات نفس نظام الترجمة المستخدم في جميع أنحاء المنصة. سترى قائمة باللغات وحقل إدخال لتقديم النص المترجم.</li>
</ul>

<h2>خطوات تعديل الترجمات</h2>
<p>لتحديث ترجمة عملة، اتبع الخطوات التالية:</p>
<ol>
<li><strong>فتح العملات:</strong> انتقل إلى صفحة قائمة العملات من القائمة الجانبية.</li>
<li><strong>اختيار عملة:</strong> ابحث عن العملة التي ترغب في تعديلها وانقر عليها أو على زر الإجراء الخاص بها لفتح ترجماتها.</li>
<li><strong>تعديل القيم:</strong> حدد موقع اللغة المحددة التي تريد تحديثها، وانقر على إجراء التعديل، واكتب القيمة المترجمة الجديدة في الحقل المخصص، ثم احفظ تغييراتك.</li>
</ol>

<h2>ماذا يحدث بعد التغييرات</h2>
<ul>
<li><strong>تحديث فوري:</strong> أي تغييرات يتم إجراؤها على ترجمات العملات تدخل حيز التنفيذ على الفور.</li>
<li><strong>الظهور:</strong> سيتم عرض النص المحدث في جميع أنحاء المنصة أينما تظهر العملة للمستخدمين.</li>
</ul>


