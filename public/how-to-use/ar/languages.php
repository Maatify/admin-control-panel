<h1>إدارة اللغات</h1>
<h2>ملخص</h2>
<p>تسمح صفحة اللغات للمسؤولين بإدارة قائمة اللغات المتاحة المستخدمة عبر النظام الأساسي. على الرغم من أن هذه الصفحة لا تتعامل مع الترجمات النصية الفعلية، إلا أنها تحدد اللغات الموجودة، ورموزها البرمجية (على سبيل المثال، "en"، "ar")، واتجاه القراءة الخاص بها (من اليسار إلى اليمين أو من اليمين إلى اليسار)، وحالتها النشطة. تستخدم أجزاء أخرى من النظام هذه اللغات لعرض المحتوى.</p>
<h2>كيفية الوصول إلى اللغات</h2>
<p>لإدارة اللغات المدعومة بالمنصة:
1. حدد موقع قائمة التنقل في الشريط الجانبي الأيسر.
2. انقر على <strong>اللغات</strong> وصلة.
3. يؤدي هذا إلى فتح صفحة قائمة اللغات الرئيسية.</p>
<h2>قائمة اللغات</h2>
<p>الواجهة الرئيسية عبارة عن جدول بيانات يعرض جميع اللغات المسجلة.</p>
<ul>
<li><strong>أعمدة الجدول:</strong><ul>
<li><strong>بطاقة تعريف:</strong> معرف النظام الفريد للغة.</li>
<li><strong>اسم:</strong> اسم اللغة الذي يمكن قراءته بواسطة الإنسان (على سبيل المثال، "الإنجليزية").</li>
<li><strong>شفرة:</strong> المعرّف البرمجي (على سبيل المثال، "en").</li>
<li><strong>اتجاه:</strong> اتجاه القراءة للغة (على سبيل المثال، <code>ltr</code> أو <code>rtl</code>).</li>
<li><strong>طلب:</strong> يحدد ترتيب الفرز الرقمي كيفية ظهور اللغات في القوائم المنسدلة عبر النظام الأساسي.</li>
<li><strong>حالة:</strong> شارة مرئية تشير إلى ما إذا كانت اللغة "نشطة" أم "غير نشطة".</li>
<li><strong>احتياطي:</strong> يعرض رمز رابط بمعرف لغة أخرى إذا تم تكوين خيار احتياطي، أو "لا شيء" مع رمز X إذا لم يكن كذلك.</li>
<li><strong>الإجراءات:</strong> يحتوي على كافة الأزرار التفاعلية الخاصة بتعديل صف اللغة.</li>
</ul>
</li>
</ul>
<h3>المرشحات والبحث</h3>
<p>أعلى الجدول، توفر الواجهة أدوات بحث وتصفية شاملة:</p>
<ul>
<li><strong>البحث العالمي:</strong> مربع إدخال بحث يسمح لك بالبحث الفوري عبر الجدول. ويتميز بتأخير البحث التلقائي لمدة ثانية واحدة أثناء الكتابة، أو يمكنك الضغط على "Enter" أو النقر فوق الزر "بحث" لتشغيله على الفور. يقوم الزر "مسح" بإعادة تعيين هذا الإدخال المحدد.</li>
<li><strong>مرشحات العمود:</strong> نموذج تصفية يسمح لك بتضييق نطاق الجدول حسب حقول محددة: المعرف والاسم والرمز والاتجاه والحالة.</li>
<li><strong>زر إعادة تعيين عوامل التصفية:</strong> زر مخصص يقوم بمسح كافة عوامل تصفية الأعمدة النشطة وإعادة تعيين عرض الجدول إلى الصفحة 1.</li>
</ul>
<h2>خلق لغة</h2>
<p>عند تقديم خيار لغة جديد إلى النظام الأساسي:</p>
<ol>
<li>انقر فوق <strong>إنشاء لغة</strong> الزر الموجود أعلى الجدول.</li>
<li>سيتم فتح نموذج أو نموذج يتطلب اللغة الجديدة <strong>اسم</strong>, <strong>شفرة</strong>، و <strong>اتجاه</strong> (LTR أو RTL). يمكنك أيضًا توفير رمز اختياريًا، وتعيين الحالة النشطة الأولية، وتعيين لغة احتياطية.</li>
<li>انقر على زر حفظ/إنشاء لإرسال النموذج.</li>
<li><strong>تصديق:</strong> يتحقق النظام بدقة من أن ما تم توفيره <strong>شفرة</strong> غير موجود بالفعل. إذا حدث ذلك، يتم عرض خطأ "اللغة موجودة بالفعل".</li>
<li><strong>نتيجة:</strong> يتم إنشاء اللغة على الفور وتعيين ترتيب الفرز التالي المتاح تلقائيًا. يظهر على الفور في قائمة اللغات.</li>
</ol>
<h2>تحرير لغة</h2>
<p>على عكس نماذج التحرير المجمعة، يتم تقسيم تعديل اللغة في هذا النظام إلى إجراءات محددة للغاية لضمان سلامة البيانات.</p>
<p>من <strong>الإجراءات</strong> في قائمة اللغات، يمكنك إجراء التعديلات التالية:</p>
<ul>
<li><strong>إعدادات التحديث:</strong> انقر فوق الزر "تحرير الإعدادات" لتعديل اتجاه القراءة وأيقونة اللغة.</li>
<li><strong>اسم التحديث:</strong> يسمح لك بتغيير اسم اللغة الذي يمكن قراءته بواسطة الإنسان.</li>
<li><strong>رمز التحديث:</strong> يسمح لك بتغيير الكود البرمجي. <em>تحذير:</em> سيتحقق النظام بشكل صارم من أن الكود الجديد ليس قيد الاستخدام بالفعل بواسطة لغة أخرى.</li>
<li><strong>تحديث ترتيب الفرز:</strong> يتيح لك ضبط الأولوية الرقمية للغة يدويًا.</li>
</ul>
<ul>
<li><strong>حفظ السلوك:</strong> يتم تطبيق كل من هذه الإجراءات على الفور. عند النجاح، يتم تحديث جدول واجهة المستخدم على الفور لعرض البيانات المحدثة.</li>
</ul>
<h2>تفعيل/إلغاء تنشيط اللغة</h2>
<p>يمكنك التحكم فيما إذا كانت اللغة المسجلة نشطة حاليًا في النظام.</p>
<ol>
<li>حدد موقع صف اللغة.</li>
<li>انقر فوق <strong>فعل</strong> أو <strong>إلغاء التنشيط</strong> زر التبديل في عمود الإجراءات.</li>
<li><strong>ماذا يحدث بعد:</strong> تتغير شارة الحالة على الفور. عند إلغاء التنشيط، تتم إزالة اللغة بشكل عام من خيارات التحديد التي يواجهها المستخدم، على الرغم من أن الترجمات الحالية المرتبطة بها تظل محفوظة بشكل آمن.</li>
</ol>
<h2>إدارة اللغات الاحتياطية</h2>
<p>تقوم "اللغة الاحتياطية" بتوجيه النظام إلى عرض نص من لغة بديلة في حالة فقدان ترجمة للغة المحددة للمستخدم.</p>
<p>من <strong>الإجراءات</strong> العمود:
1.  <strong>تعيين الاحتياطي:</strong> إذا كانت اللغة تحتوي حاليًا على "بلا" مدرجة، فانقر فوق <strong>تعيين الاحتياطي</strong> الزر (رمز الرابط الأرجواني). يتم فتح مشروط مما يسمح لك بإدخال معرف لغة أخرى.
2.  <strong>مسح الاحتياطي:</strong> إذا تم تكوين خيار احتياطي حاليًا، فانقر فوق <strong>مسح التراجع</strong> الزر (رمز X الأحمر) لإزالة الخيار الاحتياطي.</p>
<h2>حذف لغة</h2>
<p>هنالك <strong>لا توجد وظيفة الحذف</strong> للغات. تتم الإشارة إلى اللغات بشكل دائم من خلال الترجمات وإعدادات المستخدم عبر النظام الأساسي. إذا لم تعد هناك حاجة للغة، فيجب على المسؤولين استخدام <strong>إلغاء التنشيط</strong> الإجراء لإخفائه من النظام الأساسي النشط.</p>
<h2>ماذا يحدث عندما تتغير اللغات</h2>
<p>لأن هذه هي قائمة اللغات المركزية للمنصة:</p>
<ul>
<li><strong>التطبيق الفوري:</strong> يسري أي تغيير في رمز اللغة أو اتجاهها أو حالتها النشطة على الفور عبر الواجهة.</li>
<li><strong>تبعيات الترجمة:</strong> يؤثر تعديل اللغة بشكل مباشر على منطقة الترجمات، حيث يتم تحميل جميع القيم النصية بناءً على اللغات والخيارات الاحتياطية المحددة هنا.</li>
</ul>
