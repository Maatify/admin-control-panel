<h1>إدارة وثائق المحتوى</h1>
<h2>1. ما هي وثائق المحتوى</h2>
<p>تمثل مستندات المحتوى النصوص الرسمية الطويلة للمنصة. على عكس تسميات واجهة المستخدم أو نص الزر، فهذه سياسات أو اتفاقيات أو إعلانات رسمية يجب على المستخدمين قراءتها أو قبولها بشكل صريح. وهي موجودة لفرض الامتثال القانوني، وتحديد قواعد النظام الأساسي، والحفاظ على مسار تدقيق صارم لما وافق عليه المستخدم بالضبط في وقت محدد.
*   <strong>أمثلة:</strong> شروط الخدمة، سياسة الخصوصية، سياسة ملفات تعريف الارتباط، اتفاقيات ترخيص المستخدم النهائي (EULA).</p>
<h2>2. البنية الأساسية</h2>
<p>يقوم النظام بنمذجة مستندات المحتوى عبر ثلاثة عناصر علائقية متميزة:</p>
<h3>نوع الوثيقة</h3>
<ul>
<li><strong>ما هو:</strong> الحاوية على أعلى مستوى. وهو يمثل الفئة الهيكلية للوثيقة.</li>
<li><strong>ما تسيطر عليه:</strong> فهو يخزن المفتاح البرمجي الأساسي، ويحدد ما إذا كان مستندًا بالغ الأهمية للنظام، ويقوم بتكوين السلوك الافتراضي لمعرفة ما إذا كانت الإصدارات الجديدة ضمن هذا النوع تتطلب قبولًا صريحًا من المستخدم.</li>
</ul>
<h3>نسخة الوثيقة</h3>
<ul>
<li><strong>ماذا تمثل:</strong> تكرار دقيق لنوع المستند (على سبيل المثال، "الإصدار 2.0"). يمثل نسخة محددة من المستند.</li>
<li><strong>حقول الدولة:</strong> يحمل أعلام حالة دورة الحياة الحرجة:<ul>
<li>الحالة النشطة (منطقية): هل هذا الإصدار نشط حاليًا في النظام؟</li>
<li>متطلبات القبول (منطقية): هل يفرض هذا الإصدار المحدد قبول المستخدم؟</li>
<li>الطابع الزمني المنشور: عندما تم نشر المستند رسميًا.</li>
<li>الطابع الزمني المؤرشف: عندما تم سحب المستند.</li>
</ul>
</li>
</ul>
<h3>ترجمة المستندات</h3>
<ul>
<li><strong>ما تحتويه:</strong> النص المترجم الفعلي لإصدار مستند محدد. إنه يحمل العنوان وبيانات تعريف SEO الاختيارية ونص HTML.</li>
<li><strong>التبعية اللغوية:</strong> ترتبط كل ترجمة بشكل صارم باللغة والإصدار المحدد، وليس بنوع المستند الشامل.</li>
</ul>
<h2>3. دورة حياة الوثيقة (حرجة)</h2>
<p>ينتقل إصدار المستند عبر جهاز حالة صارم أحادي الاتجاه.</p>
<h3>مسودة</h3>
<ul>
<li><strong>ما الذي يحدد المسودة:</strong> تعتبر النسخة بمثابة مسودة إذا لم يتم نشرها بعد.</li>
<li><strong>الرؤية:</strong> المسودات غير مرئية تمامًا للمستخدمين النهائيين وتطبيق الواجهة الأمامية. وهي موجودة فقط في لوحة الإدارة.</li>
</ul>
<h3>التحرير</h3>
<ul>
<li><strong>ما يمكن تحريره:</strong> يمكن للمسؤولين إضافة المحتوى أو تحريره أو إزالته بأمان (نص HTML الفعلي والعناوين) عندما يكون المستند في حالة المسودة.</li>
<li><strong>قيود:</strong> يفرض النظام ثباتًا صارمًا. إذا تم وضع علامة على إصدار المستند على أنه نشط أو منشور أو مؤرشف، فسيرفض النظام على الفور أي محاولة لحفظ الترجمات أو تعديلها.</li>
</ul>
<h3>نشر</h3>
<ul>
<li><strong>ماذا يحدث عند النشر:</strong> تنتقل الوثيقة إلى الحالة المنشورة.</li>
<li><strong>ما هي الحقول التي تتغير:</strong> يتم ملء الطابع الزمني المنشور.</li>
<li><strong>التأثير على الإصدارات السابقة:</strong> يقوم النشر بطبيعته بتأمين محتوى المستند. ولا يقوم تلقائيًا بأرشفة الإصدارات الأخرى ما لم يتم تكوينها بواسطة منطق عمل محدد، ولكنه يجعل الإصدار الحالي غير قابل للتغيير بشكل دائم. بمجرد نشر المستند، يجب تفعيله بشكل صريح ليتم تقديمه للمستخدمين.</li>
</ul>
<h3>الأرشفة</h3>
<ul>
<li><strong>عندما يحدث:</strong> تتم أرشفة الإصدار عندما تحل محله سياسة أحدث أو عندما لا يكون قابلاً للتطبيق من الناحية القانونية.</li>
<li><strong>تأثير:</strong> يتم ملء الطابع الزمني المؤرشف. تم إيقاف المستند نهائيًا. يمنع النظام بشكل صارم نشر المستندات المؤرشفة أو تفعيلها مرة أخرى.</li>
</ul>
<h2>4. نموذج الإصدار</h2>
<ul>
<li><strong>لماذا يوجد الإصدار:</strong> الإصدار موجود من أجل السلامة القانونية. إذا تغيرت شروط الخدمة، فلا يمكن للنظام ببساطة استبدال النص القديم، لأنه يحتاج إلى إثبات النص الذي وافق عليه المستخدم بالضبط قبل ثلاث سنوات مقابل اليوم.</li>
<li><strong>كيفية إنشاء الإصدارات:</strong> يقوم المسؤول بشكل صريح بإنشاء إصدار مستند جديد ضمن نوع مستند موجود.</li>
<li><strong>كيف يحدد النظام الإصدار "الحالي":</strong> يقوم النظام تلقائيًا بعرض المستند الفردي ضمن نوع منشور ونشط وغير مؤرشف.</li>
<li><strong>العلاقة بين الإصدارات:</strong> الإصدارات عبارة عن سجلات مستقلة مرتبطة ببعضها فقط حسب نوع المستند المشترك الخاص بها.</li>
</ul>
<h2>5. قواعد الثبات (مهم جدًا)</h2>
<ul>
<li><strong>ما يصبح مقفلاً بعد النشر:</strong> يصبح محتوى المستند بأكمله (بما في ذلك العنوان ونص HTML) مقفلاً بشكل دائم.</li>
<li><strong>ما لا يمكن تغييره:</strong> لا يمكنك تعديل الأخطاء المطبعية أو إضافة لغات جديدة أو تغيير النص القانوني.</li>
<li><strong>لماذا (النزاهة القانونية):</strong> وهذا يضمن أن النص الذي يقبله المستخدم يوم الثلاثاء لا يمكن تعديله سرًا بواسطة المسؤول يوم الأربعاء. إذا كان هناك خطأ مطبعي يحتاج إلى إصلاح أو كانت هناك فقرة بحاجة إلى التحديث، فيجب إنشاء نسخة مسودة جديدة تمامًا وترجمتها ونشرها وربما إعادة قبولها من قبل المستخدمين.</li>
</ul>
<h2>6. Translation Model</h2>
<ul>
<li><strong>كيف يتم التعامل مع اللغات المتعددة:</strong> يمكن إرفاق ترجمات متعددة اللغات بإصدار مستند واحد.</li>
<li><strong>العلاقة مع اللغات:</strong> يقوم النظام بربط الترجمات باللغات المسجلة بالمنصة.</li>
<li><strong>كيفية ربط الترجمات بالإصدارات:</strong> الترجمات مرتبطة بشكل صارم بالإصدار المحدد. وهذا يضمن أن "الإصدار 1.0" يمكن أن يحتوي على ترجمات باللغتين الإنجليزية والإسبانية، بينما يمكن أن يحتوي "الإصدار 2.0" على ترجمات باللغات الإنجليزية والإسبانية والفرنسية، دون نزف الترجمات عبر الإصدارات.</li>
</ul>
<h2>7. نموذج القبول (حرج)</h2>
<ul>
<li><strong>عندما يجب على المستخدمين قبول المستندات:</strong> إذا تم تعيين متطلبات القبول لإصدار المستند على "صحيح"، فيمكن لبوابة التطبيق إجبار المستخدمين على الموافقة صراحةً على المستند قبل الوصول إلى النظام الأساسي.</li>
<li><strong>كيف يتم تتبع القبول:</strong> يتم تسجيل القبول بشكل آمن من قبل النظام.</li>
<li><strong>العلاقة بين الإصدار والقبول:</strong> يربط سجل القبول المستخدم بشكل دائم بالإصدار الدقيق الذي قرأه، ويسجل بشكل آمن الطابع الزمني وعنوان IP وتفاصيل المتصفح لتدقيق الامتثال.</li>
</ul>
<h2>8. تدفق تفاعل المشرف</h2>
<p>يتبع سير العمل الإداري بدقة آلة حالة دورة الحياة:
1.  <strong>نوع الإنشاء:</strong> (إذا لم تكن موجودة بالفعل) حدد الفئة العامة (على سبيل المثال، "سياسة الخصوصية").
2.  <strong>إنشاء الإصدار:</strong> أنشئ نسخة مسودة جديدة ضمن النوع المختار.
3.  <strong>إضافة الترجمات:</strong> اكتب محتوى HTML واحفظه لإصدار المسودة عبر اللغات المطلوبة.
4.  <strong>نشر:</strong> قم بقفل محتوى المسودة لجعلها وثيقة نظام رسمية.
5.  <strong>فعل:</strong> اجعل الوثيقة المنشورة هي النسخة الحية والنشطة التي يتم تقديمها للمستخدمين النهائيين.
6.  <strong>أرشيف:</strong> أرشفة أي إصدارات قديمة تم استبدالها من نفس النوع لإيقافها.</p>
<h2>9. سلوك النظام</h2>
<ul>
<li><strong>عندما تصبح التغييرات سارية المفعول:</strong> تكون التغييرات التي يتم إجراؤها على المحتوى غير مرئية حتى يتم نشر الإصدار وتفعيله.</li>
<li><strong>المسودة مقابل الرؤية المنشورة:</strong> يخدم النظام الأساسي المستندات النشطة والمنشورة وغير المؤرشفة فقط.</li>
<li><strong>ما يراه المستخدمون:</strong> يرى المستخدمون النص المترجم للإصدار النشط حاليًا. إذا تم تمكين متطلبات القبول في الإصدار النشط الجديد ولم يقبلوه بعد، فسيطلب منهم النظام الأساسي القيام بذلك.</li>
</ul>
<h2>10. القيود والقواعد</h2>
<ul>
<li><strong>لا يمكن تعديل النسخة المنشورة:</strong> يمنع النظام بشكل صارم إجراء تعديلات على أي مستند نشط أو منشور أو مؤرشف.</li>
<li><strong>لا يمكن تخطي الخطوات المطلوبة:</strong> يجب نشر المستند قبل أن يتم تفعيله. لا يمكن أبدًا نشر أو تفعيل المستند المؤرشف.</li>
<li><strong>قواعد التحقق:</strong> تتطلب الترجمات إصدار مستند صالحًا ولغة صالحة. لا يمكنك قبول مستند مؤرشف.</li>
</ul>
<h2>11. العلاقة مع الوحدات الأخرى</h2>
<ul>
<li><strong>اللغات:</strong> يعتمد كليًا على وحدة اللغات لمعرفات اللغة الصالحة.</li>
<li><strong>الترجمات:</strong> تختلف بشكل أساسي عن الترجمات القياسية. تتعامل وحدة الترجمات مع مقتطفات واجهة المستخدم الديناميكية والقابلة للتحرير والتي تحل محل بعضها البعض. تتعامل مستندات المحتوى مع محتوى HTML غير قابل للتغيير ومُصدر ومتعدد الصفحات.</li>
<li><strong>المصادقة / المشرف:</strong> يخضع الوصول لإنشاء مستندات المحتوى وتحريرها ونشرها إلى نظام الأدوار والأذونات بشكل صارم.</li>
</ul>
