<h1>إدارة إعدادات التطبيق</h1>

<h2>1. نظرة عامة</h2>
<p>إعدادات التطبيق هي تكوينات تتحكم في سلوك النظام الأساسي. يتيح هذا القسم للمسؤولين تبديل الميزات وضبط الحدود وتكوين منطق التكامل مباشرة من لوحة التحكم.</p>

<h2>2. تدفق تفاعل المشرف</h2>
<p>يدير المسؤولون هذه التكوينات من خلال واجهة قائمة إعدادات التطبيق.</p>

<h3>البحث والفلاتر</h3>
<p>في أعلى الصفحة، يمكنك استخدام الفلاتر للعثور بسرعة على إعدادات محددة:</p>
<ul>
    <li><strong>المعرف (ID):</strong> البحث عن طريق المعرف الدقيق للإعداد.</li>
    <li><strong>المجموعة (Group):</strong> التصفية حسب الفئة (مثل <code>system</code>، <code>security</code>).</li>
    <li><strong>المفتاح (Key):</strong> البحث عن طريق اسم الإعداد المحدد.</li>
    <li><strong>الحالة (Status):</strong> التصفية حسب الإعدادات النشطة أو غير النشطة.</li>
    <li><strong>زر البحث:</strong> يطبق الفلاتر المدخلة.</li>
    <li><strong>زر إعادة الضبط:</strong> يمسح جميع فلاتر الأعمدة ويعيد تحميل القائمة الافتراضية.</li>
</ul>

<h3>البحث العام</h3>
<p>يوجد شريط بحث سريع أسفل الفلاتر:</p>
<ul>
    <li><strong>بحث سريع:</strong> اكتب للبحث عبر المجموعة أو المفتاح أو القيمة في نفس الوقت.</li>
    <li><strong>زر المسح:</strong> يزيل نص البحث العام.</li>
</ul>

<h3>قائمة الإعدادات (الجدول)</h3>
<p>يعرض الجدول جميع الإعدادات المتاحة مع الأعمدة التالية:</p>
<ul>
    <li><strong>المعرف (ID):</strong> المعرف الفريد للإعداد.</li>
    <li><strong>المجموعة (Group):</strong> الفئة التي ينتمي إليها الإعداد.</li>
    <li><strong>المفتاح (Key):</strong> اسم الإعداد.</li>
    <li><strong>القيمة (Value):</strong> القيمة المكونة الحالية.</li>
    <li><strong>النوع (Type):</strong> يشير إلى تنسيق القيمة.</li>
    <li><strong>الحالة (Status):</strong> يشير إلى ما إذا كان الإعداد نشطًا أم غير نشط حاليًا.</li>
    <li><strong>الإجراءات (Actions):</strong> يحتوي على أزرار للتفاعل مع الإعداد.</li>
</ul>

<h3>المؤشرات المرئية</h3>
<ul>
    <li><strong>إعدادات محمية (🔒 أيقونة قفل):</strong> بعض الإعدادات بالغة الأهمية للنظام ولا يمكن تعديلها. تظهر هذه برمز قفل بجوار مفتاحها.</li>
    <li><strong>إعدادات معزولة (⚠️ أيقونة تحذير):</strong> تظهر الإعدادات التي لم يعد النظام يتعرف عليها مع رمز تحذير. يمكن إلغاء تنشيطها فقط.</li>
</ul>

<h2>3. كيفية تعديل إعداد</h2>
<p>لتعديل إعداد موجود:</p>
<ol>
    <li>حدد موقع الإعداد في القائمة باستخدام البحث أو الفلاتر.</li>
    <li>انقر فوق الزر <strong>تعديل (Edit)</strong> في عمود الإجراءات لذلك الصف.</li>
    <li>ستفتح نافذة <strong>تعديل إعداد التطبيق</strong> على شاشتك.</li>
    <li>قم بتعديل <strong>القيمة (Value)</strong> في حقل الإدخال المقدم.</li>
    <li>إذا لزم الأمر، قم بتغيير <strong>النوع (Type)</strong> من القائمة المنسدلة.</li>
    <li>انقر فوق الزر <strong>حفظ التغييرات (Save Changes)</strong> داخل النافذة.</li>
    <li>ستظهر رسالة نجاح، وستغلق النافذة، وسيتم تحديث الجدول تلقائيًا لإظهار القيمة المحدثة.</li>
</ol>

<h2>4. كيفية إنشاء إعداد</h2>
<p>إذا كان حسابك يمتلك الصلاحيات اللازمة، فيمكنك إنشاء إعدادات جديدة:</p>
<ol>
    <li>انقر فوق الزر <strong>إنشاء إعداد (Create Setting)</strong> الموجود بالقرب من فلاتر البحث.</li>
    <li>ستفتح نافذة إنشاء على شاشتك.</li>
    <li>املأ حقول الإدخال المطلوبة (المجموعة، المفتاح، القيمة، والنوع).</li>
    <li>انقر فوق الزر <strong>حفظ (Save)</strong> داخل النافذة.</li>
    <li>ستظهر رسالة نجاح، وستغلق النافذة، وسيتم تحديث الجدول تلقائيًا لإظهار الإعداد الذي تم إنشاؤه حديثًا.</li>
</ol>

<h2>5. إرشادات الاستخدام</h2>
<ul>
    <li><strong>نشط مقابل غير نشط:</strong> يمكنك تغيير حالة الإعداد بالنقر فوق زر التبديل في عمود الإجراءات لذلك الصف. تظهر رسالة نجاح ويتم تحديث الجدول لعرض الحالة الجديدة.</li>
    <li><strong>الإعدادات المقفلة:</strong> لا يمكنك تعديل الإعدادات المميزة برمز 🔒. تقيد الواجهة التعديل لهذه التكوينات.</li>
    <li><strong>تحديثات فورية:</strong> أي تغييرات يتم إجراؤها وحفظها في هذه الواجهة ستظهر على الفور في الجدول.</li>
</ul>


