<h1>إدارة إعدادات التطبيق</h1>
<h2>1. ما هي إعدادات التطبيق</h2>
<p>إعدادات التطبيق هي تكوينات ديناميكية تتحكم في سلوك وقت تشغيل النظام الأساسي. وهي تعمل كمحرك تكوين، مما يسمح للمسؤولين بتبديل الميزات وضبط الحدود وتكوين منطق التكامل الخارجي مباشرة من لوحة التحكم دون الحاجة إلى تدخل المطور.</p>
<ul>
<li><strong>الفرق بين التكوين الثابت وإعدادات التطبيق:</strong> يجب أن تظل تكوينات البنية الأساسية الأساسية (مثل منافذ الخادم) آمنة تمامًا ولا يتم تغييرها إلا أثناء النشر. وعلى العكس من ذلك، تمثل إعدادات التطبيق متغيرات منطق الأعمال الديناميكية التي يمكن للمسؤولين تعديلها بشكل آمن أثناء التنقل من خلال واجهة المستخدم.</li>
</ul>
<h2>2. البنية الأساسية</h2>
<p>تعتمد بنية إعداد التطبيق على اقتران صارم بين القيمة الرئيسية والمفتاحية:</p>
<h3>مفتاح الإعداد</h3>
<ul>
<li><strong>المعرف الفريد:</strong> يتم تعريف الإعداد بشكل فريد من خلال الجمع بين مجموعته ومفتاحه.</li>
<li><strong>اصطلاحات التسمية:</strong> وعادةً ما يتم تخصيص مساحة أسماء لها باستخدام التدوين النقطي داخليًا (على سبيل المثال، <code>system.timezone</code>)، ولكن تتم إدارتها كمكونات مجموعة متميزة (على سبيل المثال، "النظام") ومكونات رئيسية (على سبيل المثال، "المنطقة الزمنية").</li>
</ul>
<h3>تحديد القيمة</h3>
<ul>
<li><strong>نوع البيانات:</strong> يعلن كل إعداد بوضوح عن نوع منطقي: نص، أو عدد صحيح، أو منطقي، أو JSON.</li>
<li><strong>تنسيق التخزين:</strong> بغض النظر عن النوع المنطقي، يتم تخزين كافة القيم وعرضها بشكل منطقي. يتولى النظام تحويل القيمة إلى التنسيق الصحيح خلف الكواليس.</li>
</ul>
<h3>التجميع</h3>
<ul>
<li><strong>فئات:</strong> يقوم تصنيف المجموعة بجمع الإعدادات ذات الصلة معًا، مما يسمح للنظام بالاستعلام عن كتل التكوين بأكملها مرة واحدة (على سبيل المثال، تحميل الكل <code>smtp</code> إعدادات).</li>
</ul>
<h3>البيانات الوصفية</h3>
<ul>
<li><strong>علامة قابلة للتحرير:</strong> تحتوي الإعدادات على علامة حالة نشطة. يتم تجاهل الإعدادات غير النشطة بشكل فعال بواسطة التطبيق.</li>
<li><strong>علامة النظام:</strong> يمكن وضع علامة على الإعدادات على أنها محمية (لا يمكن تعديلها أو تعطيلها بواسطة واجهة المستخدم) وإدراجها في القائمة البيضاء (يجب التعرف عليها بواسطة رمز التطبيق لتكون صالحة).</li>
</ul>
<h2>3. نموذج التخزين</h2>
<ul>
<li><strong>بنية البيانات:</strong> تتكون الإعدادات من خصائص مثل المعرف والمجموعة والمفتاح والقيمة والنوع والحالة النشطة.</li>
<li><strong>المفتاح → تعيين القيمة:</strong> يقوم النظام بتعيين المجموعة والمفتاح لقيمة واحدة بشكل فريد.</li>
<li><strong>استخدام هيكل JSON:</strong> إذا تم تعريف أحد الإعدادات على أنه JSON، فإنه يخزن تنسيق JSON منظم.</li>
<li><strong>تطبيع:</strong> تتم إدارة الإعدادات كسجل مسطح وعالي الأداء.</li>
</ul>
<h2>4. تدفق الاسترجاع</h2>
<p>عندما يحتاج التطبيق إلى قيمة التكوين:
1.  <strong>كيف يتم تحميل الإعدادات:</strong> يقوم التطبيق باسترداد الإعداد باستخدام مجموعته ومفتاحه.
2.  <strong>تَحَقّق:</strong> يتحقق النظام من الإعداد مقابل قائمة بيضاء صارمة للتأكد من أن الإعداد المطلوب معروف بالفعل للنظام.
3.  <strong>بحث النظام:</strong> يقوم النظام بإجراء بحث فوري لاسترداد القيمة الحالية.
4.  <strong>صب:</strong> يقوم النظام بتنسيق القيمة المستردة حسب نوعها المحدد قبل استخدامها.</p>
<h2>5. استراتيجية التخزين المؤقت</h2>
<ul>
<li><strong>طبقة ذاكرة التخزين المؤقت:</strong> لا توجد طبقة تخزين مؤقت مطبقة لإعدادات التطبيق.</li>
<li>يُجري كل طلب فحصًا مباشرًا وفي الوقت الفعلي لضمان الاتساق المطلق.</li>
</ul>
<h2>6. تحديث التدفق</h2>
<ol>
<li><strong>كيف يقوم المشرف بتحديث الإعداد:</strong> يقوم المسؤول بتحرير قيمة الإعداد عبر واجهة المستخدم والنقر على حفظ. تتم معالجة المعلومات من قبل النظام.</li>
<li><strong>قواعد التحقق:</strong> يتحقق النظام أولاً من أن الإعداد غير مقفل. ثم يقوم بعد ذلك بالتحقق بدقة من صحة القيمة المقدمة مقابل النوع المعلن (على سبيل المثال، التأكد من أن النص الذي تم إدخاله لإعداد عدد صحيح هو في الواقع رقم). إذا فشل التحقق من الصحة، يقوم النظام بحظر التحديث ويعرض خطأ.</li>
<li><strong>ماذا يحدث بعد التحديث:</strong> يقوم النظام بتحديث القيمة على الفور.</li>
<li><strong>تأثير فوري:</strong> نظرًا لعدم وجود طبقة تخزين مؤقت، يعمل النظام كمصدر مطلق للحقيقة. التحديث يسري على الفور. وفي المرة التالية التي يطلب فيها النظام الأساسي هذا الإعداد، فإنه يتلقى القيمة الجديدة.</li>
</ol>
<h2>7. سلوك وقت التشغيل</h2>
<p>تُستخدم إعدادات التطبيق عبر النظام الأساسي للتحكم في قواعد العمل ديناميكيًا.</p>
<ul>
<li><strong>كيف تؤثر الإعدادات على سلوك النظام:</strong> أنها بمثابة بوابات مشروطة داخل المنصة.</li>
<li><strong>أمثلة:</strong><ul>
<li><strong>التبديل (منطقي):</strong> أ <code>feature.maintenance_mode</code> الإعداد المستخدم لمنع تسجيلات دخول المستخدم.</li>
<li><strong>الحدود (عدد صحيح):</strong> أ <code>security.max_login_attempts</code> يتم تطبيق الحد قبل قفل الحساب.</li>
<li><strong>التكوينات (JSON):</strong> ان <code>integration.payment_gateways</code> الإعداد الذي يخزن مجموعة من الموفرين الممكّنين لشاشة الخروج.</li>
</ul>
</li>
</ul>
<h2>8. تدفق تفاعل المشرف</h2>
<p>يدير المسؤولون هذه التكوينات من خلال واجهة قائمة إعدادات التطبيق المحددة:
*   <strong>أعمدة الجدول:</strong> يعرض الجدول المعرف والمجموعة والمفتاح والقيمة والنوع والحالة والإجراءات. يتضمن شارات مرئية للسياق (على سبيل المثال، أيقونة 🔒 قفل للإعدادات المحمية، و⚠️ أيقونة تحذير للإعدادات المعزولة التي لم تتعرف عليها القائمة البيضاء للنظام).
*   <strong>إعدادات التحرير:</strong> يقوم المسؤولون بالنقر فوق إجراء تحرير في صف ما لتعديل القيمة والنوع المنطقي.
*   <strong>حفظ التغييرات:</strong> يؤدي إرسال النموذج إلى تحديث النظام على الفور، ويتم تحديث جدول واجهة المستخدم.</p>
<h2>9. القيود والقواعد</h2>
<p>يفرض النظام حوكمة صارمة على ما يمكن تعديله:
*   <strong>محمي بالنظام:</strong> يقوم النظام بترميز إعدادات البنية التحتية الحيوية (على سبيل المثال، <code>system.base_url</code>) والتي تم حظرها تمامًا من التعديل عبر واجهة المستخدم. سيتم حظر محاولة تغييرها من قبل النظام.
*   <strong>سياسة القائمة البيضاء:</strong> يضمن النظام بشكل صارم عدم إمكانية إنشاء إعدادات غير صالحة أو غير معروفة. يمكن إنشاء أو الاستعلام عن مفاتيح الإعداد التي تم الإعلان عنها بشكل صريح في القائمة البيضاء الداخلية للتطبيق فقط.</p>
<h2>10. العلاقة مع الوحدات الأخرى</h2>
<ul>
<li><strong>نظام المصادقة/الإدارة:</strong> تؤثر التغييرات في إعدادات الأمان (مثل حدود المهلة) فورًا على كيفية معالجة النظام الأساسي لجلسات المستخدم.</li>
<li><strong>وثائق المحتوى والتعريب:</strong> يتم فصل إعدادات التطبيق تمامًا عن اللغات والترجمات. إنهم يديرون منطق النظام، وليس النصوص المترجمة أو إصدارات المستندات القانونية.</li>
</ul>
<h2>11. الحدود</h2>
<ul>
<li><strong>ما ينتمي إلى إعدادات التطبيق:</strong> تبديل الميزات الديناميكية، وعناوين توجيه البريد الإلكتروني الإدارية، وحدود الصفحات، وعلامات التكامل النشطة.</li>
<li><strong>ما لا يجب تخزينه هنا:</strong><ul>
<li><strong>أسرار:</strong> لا يجب أبدًا تخزين الرموز المميزة لواجهة برمجة التطبيقات (API)، وكلمات مرور قاعدة البيانات، ومفاتيح التشفير هنا؛ إنهم ينتمون حصريًا إلى تكوين البنية التحتية الأساسية.</li>
<li><strong>محتوى كبير:</strong> لا يجب تخزين كتل HTML الكبيرة أو السياسات القانونية هنا؛ إنهم ينتمون إلى وحدة مستندات المحتوى.</li>
<li><strong>الترجمات:</strong> تنتمي السلاسل النصية التي تواجه المستخدم إلى وحدة الترجمات.</li>
</ul>
</li>
</ul>
<h2>12. المخاطر وسوء الاستخدام</h2>
<ul>
<li><strong>مخاطر سوء الاستخدام:</strong> نظرًا لأن التغييرات تصبح سارية على الفور، فإن إدخال تكوين غير صالح (على سبيل المثال، تعيين حد للصفحات على <code>0</code> أو <code>1000000</code>) يمكن أن يؤدي إلى كسر تخطيطات واجهة المستخدم على الفور أو التسبب في مشكلات في الأداء على مستوى النظام الأساسي.</li>
<li><strong>أنماط الاستخدام الخاطئة:</strong> يحاول المسؤولون استخدام إعدادات التطبيق لتخزين التفضيلات الخاصة بالمستخدم أو السلاسل المترجمة الكبيرة، مما ينتهك الحدود المعمارية للوحدة.</li>
</ul>
