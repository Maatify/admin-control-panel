<h1>إدارة إعدادات النظام — نظرة عامة</h1>
<h2>1. الغرض من وحدة الإعدادات</h2>
<p>تعمل وحدة الإعدادات كمركز تحكم مركزي لتكوين سلوك النظام الأساسي وإدارة محتواه الرسمي الطويل. يستخدم المسؤولون هذا النظام للتحكم في تكوينات التطبيق الديناميكية (تبديل الميزات، ومفاتيح التكامل الخارجية) والمستندات القانونية أو وثائق السياسة (شروط الخدمة، وسياسات الخصوصية) مباشرةً من لوحة التحكم. إنه موجود للتأكد من أن كلاً من المعلمات التشغيلية للنظام والاتفاقيات الملزمة قانونًا المقدمة للمستخدمين يمكن التحكم فيها وإصدارها ومراجعتها بشكل آمن من خلال الواجهة.</p>
<h2>2. أنواع الإعدادات في النظام</h2>
<p>هناك نوعان متميزان من الإعدادات التي تتم إدارتها ضمن هذه الوحدة:</p>
<ul>
<li><strong>وثائق المحتوى:</strong> يمثل نصوصًا رسمية منقحة مثل الاتفاقيات القانونية أو سياسات الخصوصية أو وثائق الامتثال. هذه هي كيانات منظمة تتعقب المراجعات، وتدعم الترجمات متعددة اللغات لكل إصدار، وتتطلب بشكل اختياري قبولًا صريحًا من المستخدمين النهائيين.</li>
<li><strong>إعدادات التطبيق (إعدادات التطبيق):</strong> يمثل تكوينات قيمة المفتاح الفنية التي تتحكم في منطق التطبيق. يتم تجميع هذه الإعدادات بشكل منطقي (على سبيل المثال، النظام، الميزات، الحدود) ويتم فرض أنواع بيانات صارمة (على سبيل المثال، نص، عدد صحيح، منطقي) لضمان قراءة النظام الأساسي للتكوينات بأمان.</li>
</ul>
<h2>3. البنية الأساسية للإعدادات</h2>
<p>يقسم النظام التكوين إلى منطقتين متميزتين:</p>
<ul>
<li><strong>إعدادات التطبيق:</strong> هذا هو مخزن القيمة الرئيسية المكتوبة بدقة. يتم تحديد الإعدادات من خلال مجموعة مكونة من مجموعة ومفتاح (على سبيل المثال، المجموعة: "الأمان"، المفتاح: "max_login_attempts"). يقوم النظام بتخزين القيمة ونوعها الصريح وعلامة الحالة النشطة.</li>
<li><strong>وثائق المحتوى:</strong> هذا نظام منظم للغاية ويتم التحكم في إصداره. وتتكون من ثلاثة مستويات هرمية:<ul>
<li><strong>نوع الوثيقة:</strong> يحدد الفئة الواسعة (على سبيل المثال، شروط الخدمة). فهو يحدد ما إذا كانت الوثيقة من متطلبات النظام وما إذا كانت تتطلب قبول المستخدم بشكل افتراضي.</li>
<li><strong>نسخة الوثيقة:</strong> تكرار محدد لنوع المستند (على سبيل المثال، الإصدار 2.0). وهو يحتفظ ببيانات وصفية حول حالته، مثل ما إذا كان نشطًا أو منشورًا أو مؤرشفًا.</li>
<li><strong>ترجمة المستندات:</strong> المحتوى الفعلي المترجم والطويل (العنوان ونص HTML) المرتبط بإصدار مستند محدد ولغة معينة.</li>
</ul>
</li>
</ul>
<h2>4. وثائق المحتوى (عالية المستوى)</h2>
<ul>
<li><strong>ما يمثلونه:</strong> تمثل مستندات المحتوى محتوى موثوقًا يجب على المستخدمين النهائيين قراءته أو الموافقة عليه.</li>
<li><strong>لماذا يتم فصلهم عن الترجمات:</strong> بينما تتعامل منطقة الترجمات مع مقتطفات نصية قصيرة على مستوى واجهة المستخدم (مثل "زر الحفظ" أو "رسالة الترحيب")، تتعامل مستندات المحتوى مع الصفحات الكاملة بتنسيق HTML. والأهم من ذلك، أن مستندات المحتوى غير قابلة للتغيير بمجرد نشرها. فهي تتتبع إصدارات مختلفة (بحيث يعرف النظام بالضبط إصدار شروط الخدمة الذي وافق عليه المستخدم في 2024 مقابل 2026)، في حين أن الترجمات القياسية تقوم فقط بالكتابة فوق النص الحالي دون الحفاظ على السياق التاريخي.</li>
<li><strong>دورهم في النظام:</strong> إنهم بمثابة العمود الفقري القانوني والامتثال للمنصة، حيث يقومون بفرض بوابات قبول المستخدم قبل السماح بالوصول إلى ميزات معينة في النظام الأساسي.</li>
</ul>
<h2>5. إعدادات التطبيق (عالية المستوى)</h2>
<ul>
<li><strong>ما نوع التكوين الذي يتحكمون فيه:</strong> إنهم يتحكمون في السلوك التشغيلي، وتبديل الميزات، وحدود المعدلات، والتكامل الخارجي.</li>
<li><strong>أمثلة:</strong> على الرغم من أن المفاتيح المحددة ديناميكية، إلا أن الواجهة تدعم المجموعات والقيم المكتوبة. على سبيل المثال سيكون الإعداد المجمع تحت <code>system</code> مع المفتاح <code>maintenance_mode</code>، لها قيمة منطقية <code>true</code>.</li>
<li><strong>كيف تؤثر على سلوك النظام:</strong> تقرأ ميزات النظام الأساسي الأساسية هذه الإعدادات في وقت التشغيل. يؤدي تغيير إعداد التطبيق إلى تعديل سلوك النظام على الفور.</li>
</ul>
<h2>6. نموذج التفاعل الإداري</h2>
<ul>
<li><strong>كيفية وصول المسؤولين إلى الإعدادات:</strong> ينتقل المسؤولون إلى قسم "الإعدادات" في لوحة التحكم، حيث يحددون إما مناطق الإدارة "إعدادات التطبيق" أو "مستندات المحتوى".</li>
<li><strong>كيفية تعديل سلوك النظام (إعدادات التطبيق):</strong> يقوم المسؤولون بإنشاء أو تحرير زوج من المفاتيح والقيمة عبر النماذج المتوفرة، وتحديد مجموعته ونوع البيانات بشكل واضح، وتبديله إلى نشط أو غير نشط.</li>
<li><strong>كيفية تعديل سلوك النظام (مستندات المحتوى):</strong> يتبع المسؤولون تدفقًا صارمًا ومحددًا. ولا يمكنهم ببساطة تحرير مستند قانوني منشور. وبدلاً من ذلك، قاموا بما يلي:<ol>
<li>قم بإنشاء إصدار مستند جديد ضمن نوع مستند موجود.</li>
<li>قم بتوفير محتوى مترجم (ترجمات) لهذا الإصدار المحدد.</li>
<li>انشر الإصدار (الذي يقوم بأرشفة الإصدارات الأقدم بشكل اختياري).</li>
<li>ثم يبدأ النظام فورًا في تقديم الإصدار المنشور حديثًا للمستخدمين النهائيين.</li>
</ol>
</li>
</ul>
<h2>7. سلوك النظام</h2>
<ul>
<li><strong>عندما تصبح التغييرات سارية المفعول:</strong><ul>
<li>ل <strong>إعدادات التطبيق</strong>، فإن التغييرات التي تطرأ على القيمة أو حالتها النشطة تسري فورًا عبر النظام الأساسي عند الحفظ.</li>
<li>ل <strong>وثائق المحتوى</strong>، تصبح التغييرات نافذة المفعول فقط عندما يقوم إصدار المستند بنقل حالته إلى منشور ونشطة. يتم إخفاء إصدارات المسودات تمامًا من الواجهة الأمامية.</li>
</ul>
</li>
<li><strong>التحقق من الصحة أو القيود:</strong><ul>
<li><strong>إعدادات التطبيق:</strong> تتحقق الواجهة من أن القيمة المقدمة تطابق النوع المعلن (على سبيل المثال، لا يمكنك حفظ الحروف إذا تم الإعلان عن النوع كعدد صحيح). كما أنه يفرض التفرد على مجموعة Group + Key لمنع التعارضات.</li>
<li><strong>وثائق المحتوى:</strong> النظام يفرض الثبات. بمجرد نشر إصدار المستند وبدء المستخدمين في قبوله، يتم قفل محتواه الأساسي للحفاظ على السلامة القانونية. لتغيير النص، يجب على المسؤول إنشاء إصدار جديد.</li>
</ul>
</li>
</ul>
<h2>8. العلاقة مع الوحدات الأخرى</h2>
<ul>
<li><strong>العلاقة مع اللغات:</strong> تعتمد مستندات المحتوى بشكل كبير على اللغات النشطة للنظام الأساسي. عندما يكتب المسؤول المحتوى الفعلي لإصدار المستند، يجب عليه إرفاقه بلغة صالحة يوفرها النظام الأساسي.</li>
<li><strong>العلاقة مع الترجمات:</strong> ليس لوحدة الإعدادات علاقة مباشرة بمنطقة الترجمات القياسية. يحلون مشاكل مختلفة. تتعامل الترجمات مع عرض واجهة المستخدم الديناميكي للسلاسل الصغيرة. تتعامل مستندات المحتوى مع محتوى HTML طويل الشكل يتم التحكم فيه بالإصدار.</li>
<li><strong>العلاقة مع نظام الإدارة:</strong> يخضع الوصول لإدارة إعدادات التطبيق ومستندات المحتوى بشكل صارم لنظام الأدوار والأذونات.</li>
</ul>
<h2>9. حدود وحدة الإعدادات</h2>
<ul>
<li><strong>ما هي إعدادات التحكم:</strong> إنهم يتحكمون بشكل صارم في قيم التكوين الديناميكي (أزواج القيمة الرئيسية) ومستندات النظام الأساسي الطويلة ذات الإصدار (السياسات القانونية والمصطلحات والإعلانات).</li>
<li><strong>ما لا يسيطرون عليه:</strong> ولا تتحكم في صياغة أزرار واجهة المستخدم أو عناصر القائمة أو رسائل خطأ النظام (التي تتم معالجتها بواسطة الترجمات). ولا يتحكمون في حسابات المسؤول أو أدواره (التي تتم معالجتها بواسطة Admins/RBAC). ولا يتحكمون في إضافة لغات النظام الجديدة (التي يتم التعامل معها بواسطة اللغات).</li>
</ul>


