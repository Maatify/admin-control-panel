<h1>إدارة الجلسات</h1>
<h2>ملخص</h2>
<p>تمثل "الجلسة" مثيلًا محددًا للمسؤول الذي قام بتسجيل الدخول إلى النظام الأساسي على جهاز كمبيوتر أو هاتف أو متصفح ويب معين. ونظرًا لأن المسؤولين لديهم حق الوصول إلى عناصر التحكم الحساسة، فإن تتبع هذه الجلسات يساعدك على معرفة مكان وكيفية استخدام الحسابات بالضبط. في حالة فقدان جهاز أو تعرض حساب للاختراق، يمكنك استخدام أدوات إدارة الجلسات لقطع الوصول على الفور.</p>
<h2>كيفية الوصول إلى الجلسات</h2>
<p>هناك طريقتان أساسيتان لعرض وإدارة جلسات تسجيل الدخول النشطة عبر النظام الأساسي.</p>
<h3>الخيار 1: صفحة الجلسات العالمية</h3>
<p>لعرض قائمة بجميع الجلسات النشطة عبر جميع المسؤولين:
1. انظر إلى قائمة التنقل في الشريط الجانبي الأيسر.
2. انقر على <strong>الجلسات</strong>. يؤدي هذا إلى فتح قائمة الجلسات العامة.</p>
<h3>الخيار 2: لكل جلسات الإدارة</h3>
<p>لعرض الجلسات التي تخص مسؤولًا محددًا فقط:
1. في الشريط الجانبي الأيسر، انقر فوق <strong>المشرفين</strong>.
2. انقر فوق <strong>منظر</strong> الزر الموجود بجوار المسؤول المحدد الذي تريد فحصه.
3. في صفحة الملف الشخصي الخاصة بهم، انقر فوق <strong>علامة تبويب الجلسات</strong>.</p>
<h2>قائمة الجلسات</h2>
<p>سواء كنت تنظر إلى القائمة العامة أو الملف الشخصي لمسؤول محدد، فإن جدول الجلسات يوفر تفاصيل حول كل تسجيل دخول نشط.</p>
<ul>
<li><strong>أعمدة الجدول:</strong></li>
<li><strong>معرف المستخدم:</strong> معرف المسؤول الذي تنتمي إليه الجلسة.</li>
<li><strong>معرف الجلسة:</strong> المعرف الفريد لمثيل تسجيل الدخول المحدد.</li>
<li><strong>حالة:</strong> ما إذا كانت الجلسة نشطة حاليًا أو منتهية الصلاحية أو ملغاة.</li>
<li><strong>تنتهي في:</strong> التاريخ والوقت الدقيقان الذي ستنتهي فيه الجلسة بطبيعة الحال.</li>
<li><strong>إشارة الجلسة الحالية:</strong> النظام يسلط الضوء بوضوح على الخاص بك <em>الجلسة الحالية</em> حتى لا تقوم بتسجيل الخروج عن طريق الخطأ.</li>
<li><strong>المرشحات والبحث:</strong> أعلى الجدول، يمكنك النقر على شارات التصفية السريعة لعرضها على الفور <strong>الجميع</strong>, <strong>نشيط</strong>, <strong>منتهي الصلاحية</strong>، أو <strong>تم إبطاله</strong> جلسات. بالإضافة إلى ذلك، يسمح لك نموذج البحث بالتصفية حسب <strong>معرف الجلسة</strong>, <strong>معرف المشرف</strong>، و <strong>حالة</strong>. يقوم زر إعادة الضبط بمسح جميع المرشحات النشطة.</li>
</ul>
<h2>إلغاء الجلسة</h2>
<p>إذا كنت تريد إجبار أحد المسؤولين على تسجيل الخروج من جهاز معين، فيجب عليك إلغاء جلسته.</p>
<ol>
<li>حدد موقع الجلسة المحددة في القائمة (إما في صفحة الجلسات العامة أو الملف الشخصي للمسؤول).</li>
<li>انقر فوق <strong>إبطال</strong> زر الإجراء بجوار تلك الجلسة.</li>
<li><em>ملاحظة حول سلوك التأكيد:</em> عند إجراء إلغاء مجمع لجلسات محددة متعددة، يطالبك النظام صراحةً من خلال مربع حوار التأكيد ("إلغاء جلسة (جلسات) [X]؟").</li>
<li><em>ملاحظة حول الإلغاء الجزئي:</em> لا يمكنك إلغاء الجلسة جزئيًا. يؤدي النقر فوق إبطال إلى إنهاء جلسة تسجيل الدخول بأكملها على الفور.</li>
</ol>
<p>إذا كنت بحاجة إلى تسجيل خروج المسؤول <em>كل</em> الجهاز مرة واحدة، يمكنك استخدام <strong>إبطال بالجملة</strong> الإجراء متاح في الملف الشخصي للمسؤول.</p>
<h2>ماذا يحدث بعد الإلغاء</h2>
<ul>
<li><strong>يتم إنهاء الجلسة على الفور:</strong> في اللحظة التي تنقر فيها على إبطال، يقوم النظام بإبطال تسجيل الدخول المحدد هذا.</li>
<li><strong>يتم إجبار المستخدم على الخروج:</strong> إذا كان المسؤول يستخدم حاليًا اللوحة الموجودة على هذا الجهاز الذي تم إبطاله، فسيتم إعادته على الفور إلى شاشة تسجيل الدخول في المرة التالية التي ينقر فيها على رابط أو يقوم بتحديث الصفحة.</li>
<li><strong>الوصول محظور حتى إعادة تسجيل الدخول:</strong> لا يمكن للجهاز الوصول إلى لوحة الإدارة مرة أخرى حتى يقوم المسؤول بإعادة إدخال البريد الإلكتروني وكلمة المرور وإكمال عملية التحقق من المصادقة الثنائية (2FA).</li>
</ul>
<h2>حالات الاستخدام الأمني</h2>
<ul>
<li><strong>تم اكتشاف جهاز غير معروف:</strong> إذا لاحظت أنت أو مسؤول آخر جلسة نشطة من موقع أو متصفح غير مألوف، فيمكنك إبطالها على الفور لتأمين الحساب.</li>
<li><strong>المشرف ترك الشركة:</strong> عندما يستقيل أحد المسؤولين أو يتم إنهاؤه، يجب عليك على الفور إلغاء جميع جلساته النشطة بشكل جماعي وتعطيل حسابه لضمان عدم تمكنه من الوصول إلى النظام الأساسي من جهاز شخصي.</li>
<li><strong>تنظيف الجهاز المشترك:</strong> إذا ترك المسؤول نفسه عن طريق الخطأ مسجلاً الدخول على جهاز كمبيوتر عام أو مشترك، فيمكنه (أو أنت) إلغاء تلك الجلسة المحددة عن بعد دون التأثير على وصوله إلى كمبيوتر العمل الأساسي الخاص به.</li>
</ul>
<h2>ملاحظات هامة</h2>
<ul>
<li><strong>الإلغاء فوري:</strong> ليس هناك تأخير. يفقد الجهاز إمكانية الوصول في اللحظة التي يتم فيها الضغط على زر الإلغاء.</li>
<li><strong>سلامة الجلسة الحالية:</strong> انتبه دائمًا إلى <em>الجلسة الحالية</em> مؤشر للتأكد من عدم تسجيل خروجك من اللوحة عن طريق الخطأ.</li>
</ul>


