<h2>3. المهام المشتركة</h2>
<h3>تسجيل الدخول</h3>
<p>للوصول إلى النظام، يجب عليك تسجيل الدخول باستخدام بيانات الاعتماد الخاصة بك. بعد تقديم بريدك الإلكتروني وكلمة المرور، سيُطلب منك إكمال خطوة المصادقة الثنائية (2FA) للتحقق من هويتك.</p>
<h3>إدارة المشرفين</h3>
<p>يمكنك عرض قائمة بجميع المسؤولين. عند إدارة مسؤول معين، يمكنك تعديل تفاصيل ملفه الشخصي وإضافة عناوين بريده الإلكتروني أو تحديثها ومراجعة تفضيلات الإشعارات الخاصة به.</p>
<h3>تعيين الأدوار</h3>
<p>بمجرد إنشاء الأدوار (مثل "المدير" أو "الدعم")، يمكنك تعيينها للمسؤولين. إذا لم يعد الدور مناسبًا، فيمكنك إلغاء تعيينه. يمكنك أيضًا إدارة الأذونات المحددة المرتبطة بكل دور لتغيير ما يُسمح لهذا الدور بفعله.</p>
<h3>تحرير الترجمات</h3>
<p>لتحديث النص على المنصة، انتقل إلى منطقة الترجمات. يمكنك تعيين لغات لأقسام (نطاقات) مختلفة من النظام الأساسي، والبحث عن مفاتيح ترجمة محددة، وتحديث النص المترجم.</p>
<h3>إدارة المستندات</h3>
<p>عندما يلزم نشر سياسة جديدة، عليك أولاً إنشاء إصدار مستند. بمجرد صياغته، يمكنك إضافة ترجمات له. عندما تصبح جاهزًا، يمكنك نشر الإصدار وتنشيطه وأرشفة الإصدارات الأقدم إذا لزم الأمر.</p>
<h3>مراجعة سجلات النشاط</h3>
<p>لمعرفة من فعل ماذا، يمكنك التحقق من سجلات الأنشطة. وهذا يوفر مسارًا آمنًا للإجراءات التي يتم تنفيذها داخل لوحة الإدارة، مما يساعد في المساءلة واستكشاف الأخطاء وإصلاحها.</p>
<h2>4. الأمن والحماية</h2>
<ul>
<li>
<p><strong>أمان تسجيل الدخول (2FA)</strong>
  يتطلب الوصول إلى المناطق الحساسة في اللوحة المصادقة الثنائية. وهذا يعني أنه حتى لو كان شخص ما يعرف كلمة المرور الخاصة بك، فلن يتمكن من الوصول إلى النظام بدون طريقة التحقق الثانوية الخاصة بك.</p>
</li>
<li>
<p><strong>التحكم في الجلسة</strong>
  في كل مرة تقوم فيها بتسجيل الدخول، يتم إنشاء جلسة. لديك رؤية كاملة لجلساتك النشطة ويمكنك إلغاء (تسجيل الخروج) أي جلسات قديمة أو غير معروفة للحفاظ على أمان حسابك.</p>
</li>
</ul>
<h2>5. ملاحظات هامة</h2>
<ul>
<li><strong>تتطلب بعض التغييرات التنشيط:</strong> إن إنشاء إصدار مستند جديد أو إضافة لغة جديدة لا يجعله مرئيًا للمستخدمين النهائيين على الفور. يجب عليك "نشر" أو "تنشيط" هذه العناصر بشكل صريح قبل أن تدخل حيز التنفيذ.</li>
<li><strong>تؤثر الأذونات على الرؤية:</strong> تعتمد القوائم والخيارات التي تراها في لوحة الإدارة بشكل كامل على الأدوار والأذونات المخصصة لك. إذا لم تتمكن من رؤية إحدى الميزات، فهذا يعني أن حسابك لا يتمتع بحق الوصول المطلوب.</li>
<li><strong>تتطلب بعض الإجراءات التحقق:</strong> قد يؤدي تغيير تفاصيل الحساب المهمة، مثل عناوين البريد الإلكتروني أو كلمات المرور، إلى تشغيل خطوة التحقق (مثل تلقي رسالة تأكيد عبر البريد الإلكتروني) قبل الانتهاء من التغيير.</li>
</ul>


