<h1>نظرة عامة على لوحة الإدارة</h1>
<h2>1. ما هو هذا النظام؟</h2>
<p>هذا النظام عبارة عن لوحة تحكم إدارية مصممة لمساعدتك على إدارة النظام الأساسي بشكل آمن. فهو يسمح لك بتنظيم حسابات المستخدمين، وتعيين الأدوار والأذونات، وتكوين إعدادات النظام الأساسي، وإدارة المحتوى المترجم عبر لغات متعددة، والتعامل مع السياسات أو المستندات المهمة. تم تصميم النظام الأساسي مع أخذ الأمان القوي في الاعتبار، مما يضمن تتبع جميع الإجراءات والتحقق منها وحمايتها من الوصول غير المصرح به.</p>
<h2>2. المجالات الرئيسية للنظام</h2>
<ul>
<li>
<p><strong>إدارة المستخدم والمسؤول</strong>
  يسمح لك بإنشاء وإدارة الحسابات الإدارية. يمكنك تحديث ملفاتهم الشخصية وإدارة عناوين البريد الإلكتروني المرتبطة بهم وتكوين تفضيلات الإشعارات والإشراف على جلسات تسجيل الدخول النشطة الخاصة بهم.</p>
</li>
<li>
<p><strong>الأدوار والأذونات</strong>
  يوفر طريقة للتحكم في من يمكنه الوصول إلى ماذا. يمكنك إنشاء أدوار مختلفة، وتعيين أذونات محددة لهذه الأدوار، ثم منحها للمسؤولين. يمكنك أيضًا تعيين أذونات مباشرة لمستخدمين محددين لمزيد من التحكم الدقيق.</p>
</li>
<li>
<p><strong>اللغات والترجمات</strong>
  يمكنك من إدارة المنصة بلغات متعددة. يمكنك إنشاء لغات جديدة، وتعيين اللغات الاحتياطية (الافتراضية)، وتحديد مفاتيح الترجمة، وتحديث النص المترجم الفعلي لأقسام مختلفة من التطبيق.</p>
</li>
<li>
<p><strong>المحتوى والوثائق</strong>
  يتيح لك إدارة مستندات النظام الأساسي المهمة (مثل شروط الخدمة أو سياسات الخصوصية). يمكنك إنشاء أنواع مختلفة من المستندات، وإدارة إصدارات متعددة، ونشر إصدارات جديدة، وإضافة ترجمات لها، وفرض متطلبات على المستخدمين لقبول هذه المستندات.</p>
</li>
<li>
<p><strong>إعدادات النظام</strong>
  يوفر أدوات لإدارة الإعدادات والتكوينات على مستوى التطبيق. يمكنك عرض البيانات التعريفية وإنشائها وتحديثها أو تشغيل ميزات معينة في النظام الأساسي وإيقاف تشغيلها.</p>
</li>
<li>
<p><strong>الأمن والجلسات</strong>
  يوفر الإشراف على كيفية وصول المستخدمين إلى النظام. يمكنك عرض قائمة بجلسات تسجيل الدخول النشطة وإبطالها بشكل فردي أو جماعي إذا لزم الأمر.</p>
</li>
<li>
<p><strong>الإخطارات وتسجيل النشاط</strong>
  يبقيك على علم بأحداث النظام. يمكنك مراجعة الإشعارات الخاصة بك وعرض الإشعارات التاريخية والوصول إلى سجل الأنشطة التفصيلي الذي يسجل الإجراءات المتخذة داخل النظام.</p>
</li>
</ul>
