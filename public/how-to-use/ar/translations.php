<h1>إدارة الترجمات</h1>
<h2>ملخص</h2>
<p>تسمح الترجمات للمسؤولين بإدارة النص المحدد الذي يظهر عبر واجهة النظام. تملي التغييرات التي تم إجراؤها هنا بالضبط الكلمات والعبارات التي يقرأها المستخدمون على المنصة.</p>
<h2>كيفية الوصول إلى الترجمات</h2>
<ul>
<li>انتقل إلى قائمة الشريط الجانبي الأيسر.</li>
<li>تحت <strong>ترجمات</strong> القسم، ستجد خيارين أساسيين: <strong>النطاقات</strong> و <strong>المجالات</strong>.</li>
</ul>
<h2>هيكل الترجمات</h2>
<p>يقوم النظام بتجميع بيانات الترجمة إلى ثلاثة مستويات:</p>
<ul>
<li><strong>النطاقات:</strong> فئات المستوى الأعلى.</li>
<li><strong>المجالات:</strong> الفئات الفرعية التي تنتمي إلى نطاقات محددة.</li>
<li><strong>المفاتيح:</strong> معرفات النص الفعلية والترجمات المرتبطة بها بلغات مختلفة.</li>
</ul>
<h2>النطاقات</h2>
<p>عندما تنقر على <strong>النطاقات</strong> في الشريط الجانبي:</p>
<ul>
<li><strong>أعمدة الجدول:</strong> ترى جدولاً يعرض معرف النطاق والرمز والاسم والوصف والحالة النشطة والطلب.</li>
<li><strong>ما هي الأزرار الموجودة:</strong> سترى أزرار الإجراءات الخاصة بـ "Code" و"Meta" لتحرير التفاصيل الأساسية للنطاق. يمكنك أيضًا النقر مباشرةً على النطاق لفتحه.</li>
<li><strong>سلوك التنقل:</strong> يتيح لك فتح النطاق عرض النطاقات المخصصة له وإدارة المفاتيح المرتبطة على وجه التحديد بهذا النطاق.</li>
</ul>
<h2>المجالات</h2>
<p>عند عرض النطاقات المخصصة لنطاق ما (أو عرض قائمة النطاقات العامة):</p>
<ul>
<li><strong>الهيكل الكامل:</strong> سترى قائمة بالمجالات المرتبطة بالنطاق.</li>
<li><strong>سلوك التنقل:</strong> يمكنك النقر لعرض مفاتيح الترجمة المحددة التي تنتمي إلى هذا النطاق.</li>
</ul>
<h2>مفاتيح الترجمة (الأكثر أهمية)</h2>
<p>عندما تتعمق في نطاق ومجال محدد، ستصل إلى قائمة الترجمات.</p>
<ul>
<li><strong>بالضبط كيف يتم عرض قيم الترجمة:</strong> يسرد الجدول "الجزء الرئيسي" (معرف النص). بجانبه يوجد عمود مخصص يوضح القيمة المترجمة حاليًا. إذا لم تكن هناك ترجمة للغة ما، فسيتم عرض عنصر نائب "فارغ" بخط مائل. تتم الإشارة إلى اللغة نفسها بوضوح بجوار القيمة.</li>
<li><strong>بالضبط كيف يتم تحريرها:</strong> كل صف لديه <strong>رمز التحرير</strong> (قلم رصاص).</li>
<li><strong>نوع الإدخال:</strong> يؤدي النقر فوق أيقونة "تحرير" إلى فتح نافذة منبثقة "تحرير الترجمة". يوجد داخل هذا النموذج حقل إدخال نص (أو منطقة نصية) حيث تكتب الترجمة الجديدة. يحترم إدخال النص تلقائيًا اتجاه القراءة (على سبيل المثال، من اليسار إلى اليمين أو من اليمين إلى اليسار) للغة المحددة.</li>
<li><strong>آلية الحفظ:</strong> يجب عليك النقر فوق <strong>يحفظ</strong> الزر الموجود أسفل النموذج لتطبيق التغييرات. لا توجد خاصية الحفظ التلقائي.</li>
</ul>
<h2>تدفق تفاعل المستخدم</h2>
<p>لتحرير الترجمة:
1. <strong>ما ينقر عليه المسؤول:</strong> انقر فوق <strong>رمز التحرير</strong> بجوار المفتاح واللغة المحددين اللذين تريد تحديثهما.
2. <strong>ما يظهر:</strong> يظهر شكل منبثق على الشاشة يحتوي على اسم المفتاح واسم اللغة وحقل إدخال النص.
3. <strong>ما الذي يتغير:</strong> اكتب الترجمة الجديدة في حقل النص ثم انقر <strong>يحفظ</strong>. تظهر رسالة نجاح ("تم حفظ الترجمة بنجاح")، ويتم إغلاق النموذج، ويتم تحديث الجدول على الفور لإظهار القيمة الجديدة.</p>
<h2>قيمة الترجمة</h2>
<ul>
<li><strong>حيث يظهر النص:</strong> تم الآن حفظ الترجمة المحدثة وسيتم عرضها للمستخدمين.</li>
<li><strong>كيف يتم التحرير:</strong> عبر نموذج التحرير المنبثق الذي يمكن الوصول إليه من جدول قائمة الترجمات.</li>
<li><strong>كيفية عرض اللغات المتعددة:</strong> يتم إدراج كل ترجمة لغة لمفتاح كصف خاص بها في الجدول، مع وضع علامة واضحة عليها بشارة توضح اسم اللغة ورمزها (على سبيل المثال، "الإنجليزية (en)").</li>
</ul>
<h2>تدفق الملاحة</h2>
<ol>
<li><strong>الشريط الجانبي ← الترجمات ← النطاقات:</strong> ابدأ بعرض الفئات ذات المستوى الأعلى.</li>
<li><strong>النطاقات → المجالات:</strong> انقر فوق نطاق محدد لرؤية الفئات الفرعية المخصصة له (المجالات).</li>
<li><strong>المجالات → المفاتيح:</strong> انقر داخل أحد النطاقات لرؤية جدول قائمة الترجمات الفعلي الذي يتم فيه التحرير.</li>
</ol>
<h2>المرشحات / البحث</h2>
<p>عند عرض قائمة الترجمات:</p>
<ul>
<li><strong>مدخلات البحث:</strong> يتوفر مربع بحث للعثور على أسماء مفاتيح محددة أو قيم ترجمة.</li>
<li><strong>قوائم التصفية المنسدلة:</strong> يتم توفير قائمة منسدلة لتصفية الجدول لإظهار لغة معينة فقط.</li>
</ul>
<h2>حفظ السلوك</h2>
<ul>
<li><strong>هل يوجد زر حفظ؟</strong> نعم، يحتوي نموذج "تحرير الترجمة" على زر حفظ واضح.</li>
<li><strong>هل هو الحفظ التلقائي؟</strong> لا.</li>
<li><strong>متى ينطبق التغيير؟</strong> يتم حفظ التغيير على الفور في النظام عند النقر فوق "حفظ"، ويتم تحديث الجدول على الفور.</li>
</ul>
