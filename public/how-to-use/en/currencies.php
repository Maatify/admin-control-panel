<h1>Managing Currencies</h1>
<h2>Overview</h2>
<p>The Currencies page allows you to view and manage the list of monetary currencies available in the platform. You can see details for each currency and adjust how their names are displayed in different languages.</p>

<h2>How to Access Currencies</h2>
<ol>
<li>Locate the left sidebar navigation menu.</li>
<li>Click on the <strong>Currencies</strong> link.</li>
<li>The main Currencies List page will open.</li>
</ol>

<h2>Currencies List</h2>
<p>The main interface displays a table of all available currencies.</p>
<ul>
<li><strong>Table Information:</strong> The table displays standard details identifying each currency, such as its name (e.g., "US Dollar"), code (e.g., "USD"), symbol (e.g., "$"), and current active status.</li>
<li><strong>Available Actions:</strong> The table provides options to interact with each currency, including the ability to manage how the currency's name is translated.</li>
</ul>

<h2>Viewing Currency Translations</h2>
<p>To view or change how a currency appears in different languages:</p>
<ol>
<li>Locate the specific currency in the Currencies List table.</li>
<li>Click on the currency or use the available action to open its translations.</li>
<li>This will navigate you to a dedicated page showing the translations for that specific currency.</li>
</ol>

<h2>Currency Translations</h2>
<p>Each currency can have its own translations, ensuring that users see familiar currency names in their selected language.</p>
<ul>
<li><strong>What you can edit:</strong> You can manage the localized text associated with the currency, such as its display name or label in various languages.</li>
<li><strong>Translation process:</strong> Currency translations follow the same translation system used across the platform. You will see a list of languages and an input field to provide the translated text.</li>
</ul>

<h2>User Flow for Editing Translations</h2>
<p>To update a currency's translation, follow these steps:</p>
<ol>
<li><strong>Open currencies:</strong> Navigate to the Currencies List page from the sidebar.</li>
<li><strong>Select a currency:</strong> Find the currency you wish to edit and click on it or its action button to open its translations.</li>
<li><strong>Edit values:</strong> Locate the specific language you want to update, click the edit action, type the new translated value in the provided field, and save your changes.</li>
</ol>

<h2>What Happens After Changes</h2>
<ul>
<li><strong>Immediate Update:</strong> Any changes made to currency translations take effect immediately.</li>
<li><strong>Visibility:</strong> The updated text will be displayed across the platform wherever the currency appears to users.</li>
</ul>


